use std::{io::Write, path::Path};

pub fn resolve_stub(target_dir: &Path) -> crate::Result<std::path::PathBuf> {
    let gen_dir = target_dir.join("tairitsu").join("generated");
    std::fs::create_dir_all(&gen_dir)?;
    let output = gen_dir.join("hikari_icons.rs");

    let mut f = std::fs::File::create(&output)?;
    writeln!(f, "// @generated by tairitsu-packager — DO NOT EDIT (stub)")?;
    writeln!(f)?;
    writeln!(f, "crate::impl_icons! {{}}")?;

    Ok(output)
}
