fn locale() -> &'static crate::i18n::Translations {
    crate::i18n::translations()
}

fn find_workspace_root(manifest_dir: &std::path::Path) -> crate::Result<std::path::PathBuf> {
    let output = std::process::Command::new("cargo")
        .args([
            "metadata",
            "--no-deps",
            "--locked",
            "--format-version",
            "1",
            "--manifest-path",
            manifest_dir.join("Cargo.toml").to_str().unwrap(),
        ])
        .output()?;

    let metadata: serde_json::Value = serde_json::from_slice(&output.stdout)?;
    let workspace_root = metadata
        .get("workspace_root")
        .and_then(|v| v.as_str())
        .map(std::path::PathBuf::from)
        .ok_or_else(|| {
            crate::TairitsuPackagerError::BuildError("Failed to find workspace root".to_string())
        })?;

    Ok(workspace_root)
}

pub fn resolve_hikari_icons(
    manifest_dir: &std::path::Path,
    offline: bool,
    _verbose: bool,
) -> crate::Result<std::path::PathBuf> {
    let meta = crate::icons::read_consumer_metadata(manifest_dir)?;

    if meta.sets.is_empty() {
        let target_dir = manifest_dir.join("target");
        return crate::icons::hikari_resolver::resolve_stub(&target_dir);
    }

    let cache_root = crate::icons::resolve_cache_root(Some(manifest_dir));

    let cache = crate::icons::IconCache::new(cache_root, offline);
    let result = crate::icons::resolve(&meta, &cache)?;

    if result.sets.is_empty() {
        let target_dir = manifest_dir.join("target");
        return crate::icons::hikari_resolver::resolve_stub(&target_dir);
    }

    let output = manifest_dir
        .join("target")
        .join("tairitsu")
        .join("generated")
        .join("hikari_icons.rs");
    if let Some(parent) = output.parent() {
        std::fs::create_dir_all(parent)?;
    }

    let mut f = std::fs::File::create(&output)?;
    use std::io::Write as _;
    writeln!(f, "// @generated by tairitsu-packager — DO NOT EDIT")?;
    writeln!(f)?;
    writeln!(f, "crate::impl_icons! {{")?;
    for resolved in &result.sets {
        let view_box = resolved.source.view_box;
        let set_ident = resolved.source.name.replace('-', "_").to_uppercase();
        let mut icons: Vec<_> = resolved.icons.iter().collect();
        icons.sort_by(|a, b| a.0.cmp(b.0));
        for (name, data) in &icons {
            let safe = name.replace('-', "_").to_uppercase();
            let safe = if safe
                .as_bytes()
                .first()
                .map(|b| b.is_ascii_digit())
                .unwrap_or(false)
            {
                format!("_{}", safe)
            } else {
                safe
            };
            let const_name = format!("{}_{}", set_ident, safe);
            writeln!(
                f,
                "    {:?} => {{ ident: {}, path: {:?}, view_box: {:?} }},",
                name, const_name, data.path_d, view_box
            )?;
        }
    }
    writeln!(f, "}}")?;

    crate::log_info!(
        "Generated {} → {} icon sets",
        output.display(),
        result.sets.len()
    );
    Ok(output)
}

/// 构建 wasm32-wasip2 格式的 WASM Component，
/// 但使用 WIT Component Model 及 tairitsu browser-glue 实现浏览器互操作。
///
/// 构建步骤：
/// 1. 检查 wasm32-wasip2 toolchain 已安装
/// 2. `cargo build --target wasm32-wasip2 --lib`
/// 3. 将 .wasm 组件拷贝到输出目录
/// 4. 将 browser-glue/dist/ 拷贝到输出目录
/// 5. 生成宿主 HTML（通过 browser-glue 加载组件）
pub fn build_component(
    config: &Config,
    release: bool,
    multi: Option<std::sync::Arc<MultiProgress>>,
    verbose: bool,
) -> crate::Result<()> {
    let build_start = Instant::now();
    let pb_raw = ProgressBar::new(5);
    pb_raw.set_style(
        ProgressStyle::with_template("  {spinner:.bold.cyan}  {prefix:.bold.dim}  {wide_msg}")
            .unwrap()
            .tick_strings(&["⠋", "⠙", "⠹", "⠸", "⠼", "⠴", "⠦", "⠧", "⠇", "⠏"]),
    );
    pb_raw.enable_steady_tick(Duration::from_millis(80));
    // Attach to MultiProgress when in watch mode so pb.println() lines
    // scroll above the persistent status bars instead of below them.
    let pb = match &multi {
        Some(m) => m.add(pb_raw),
        None => pb_raw,
    };

    crate::logfmt::set_active_pb(&pb);

    let build_result: crate::Result<()> = (|| {
        let b = &locale().build;
        // -- 1/5  check target -----------------------------------------------------
        pb.set_prefix("[1/5]");
        pb.set_message(&b.step_check_target);
        let t = Instant::now();
        check_wasip2_target()?;
        crate::log_ok!("{:<20} {:.1?}", b.step_check_target, t.elapsed());
        pb.inc(1);

        // -- 2/5  compile ----------------------------------------------------------
        pb.set_prefix("[2/5]");
        pb.set_message(&b.step_compile);
        let t = Instant::now();

        // Resolve hikari-icons before compiling
        let icons_rs = resolve_hikari_icons(&config.manifest_dir, false, verbose).ok();

        let wasm_path =
            build_wasm_component(config, release, pb.clone(), verbose, icons_rs.as_deref())?;
        crate::log_ok!("{:<20} {:.1?}", b.step_compile, t.elapsed());
        pb.inc(1);

        // -- 3/5  bundle assets ----------------------------------------------------
        pb.set_prefix("[3/5]");
        pb.set_message(&b.step_bundle);
        let t = Instant::now();

        // Resolve output_dir relative to manifest_dir
        let output_dir = if config.build.output_dir.is_relative() {
            config.manifest_dir.join(&config.build.output_dir)
        } else {
            config.build.output_dir.clone()
        };

        let dest_wasm = output_dir.join(format!("{}.wasm", config.package.name.replace('-', "_")));

        crate::log_info!("{}", b.creating_output_dir);
        std::fs::create_dir_all(&output_dir)?;

        crate::log_info!("{}", b.copying_wasm);
        std::fs::copy(&wasm_path, &dest_wasm)?;

        crate::log_info!("{}", b.copying_glue);
        copy_browser_glue_with_output_dir(config, &output_dir, &pb)?;

        crate::log_info!("{}", b.copying_public);
        copy_static_public_assets_with_output_dir(config, &output_dir)?;

        write_browser_glue_bundle(config, &output_dir)?;

        crate::log_info!("{}", b.compiling_scss);
        compile_project_scss_with_output_dir(config, &output_dir)?;
        crate::log_ok!("{:<20} {:.1?}", b.step_bundle, t.elapsed());
        pb.inc(1);

        // -- 4/5  component wrapper ------------------------------------------------
        pb.set_prefix("[4/5]");
        pb.set_message(&b.step_wrapper);
        let t = Instant::now();
        prepare_component_wrapper_fallback(config, &dest_wasm, &output_dir, &pb)?;
        crate::log_ok!("{:<20} {:.1?}", b.step_wrapper, t.elapsed());
        pb.inc(1);

        // -- 5/5  HTML -------------------------------------------------------------
        pb.set_prefix("[5/5]");
        pb.set_message(&b.step_generate_html);
        let t = Instant::now();
        generate_component_html_with_output_dir(config, &output_dir)?;
        crate::log_ok!("{:<20} {:.1?}", b.step_generate_html, t.elapsed());
        pb.inc(1);

        Ok(())
    })();

    pb.finish_and_clear();
    crate::logfmt::clear_active_pb();

    build_result?;

    let elapsed = build_start.elapsed();
    crate::log_ok!(
        "{}",
        crate::fmt_tmpl!(locale().build.build_complete, elapsed => format!("{:.1?}", elapsed))
    );

    let output_dir = if config.build.output_dir.is_relative() {
        config.manifest_dir.join(&config.build.output_dir)
    } else {
        config.build.output_dir.clone()
    };
    let wasm_file = output_dir.join(format!("{}.wasm", config.package.name.replace('-', "_")));
    if let Ok(meta) = std::fs::metadata(&wasm_file) {
        let size_bytes = meta.len();
        let size_str = if size_bytes >= 1_048_576 {
            format!("{:.1} MB", size_bytes as f64 / 1_048_576.0)
        } else {
            format!("{:.1} KB", size_bytes as f64 / 1024.0)
        };
        crate::log_info!(
            "{}",
            crate::fmt_tmpl!(locale().build.package_info,
                name => wasm_file.file_name().unwrap_or_default().to_string_lossy(),
                size => size_str
            )
        );
    }

    // Log successful build if in daemon mode
    if daemon::is_daemon() {
        let _ = daemon::append_build_log("component", true, None);
    }

    Ok(())
}

fn check_wasip2_target() -> crate::Result<()> {
    let output = std::process::Command::new("rustup")
        .args(["target", "list", "--installed"])
        .output()?;

    let targets = String::from_utf8_lossy(&output.stdout);
    if !targets.contains("wasm32-wasip2") {
        return Err(crate::TairitsuPackagerError::BuildError(
            locale().build.target_not_installed.to_string(),
        ));
    }

    Ok(())
}

fn build_wasm_component(
    config: &Config,
    release: bool,
    pb: ProgressBar,
    verbose: bool,
    icons_rs: Option<&std::path::Path>,
) -> crate::Result<std::path::PathBuf> {
    use std::io::BufRead;
    use std::process::Stdio;

    let pkg_name = &config.package.name;

    let mut cmd = std::process::Command::new("cargo");
    cmd.args([
        "build",
        "--locked",
        "--target",
        "wasm32-wasip2",
        "--lib",
        "--package",
        pkg_name,
        "--manifest-path",
        config.manifest_dir.join("Cargo.toml").to_str().unwrap(),
        "--message-format=json-diagnostic-rendered-ansi",
    ]);
    if release {
        cmd.arg("--release");
    } else {
        cmd.args(["--profile", "dev-wasm"]);
    }

    if let Some(path) = icons_rs {
        cmd.env("TAIRITSU_ICONS_RS", path);
    }

    cmd.stdout(Stdio::piped());
    cmd.stderr(Stdio::piped());

    let mut child = cmd.spawn()?;

    let stdout = child.stdout.take().expect("stdout should be piped");
    let stderr = child.stderr.take().expect("stderr should be piped");

    let pb_clone = pb.clone();

    fn extract_crate_name(package_id: &str) -> &str {
        if let Some(after_hash) = package_id.split('#').nth(1) {
            if let Some(name) = after_hash.split('@').next() {
                return name;
            }
        }
        package_id
    }

    let diagnostics = std::sync::Arc::new(std::sync::Mutex::new(String::new()));
    let diag_clone = diagnostics.clone();

    #[derive(Clone)]
    struct DiagEntry {
        level: String,
        message: String,
        location: Option<String>,
    }
    let diag_entries: std::sync::Arc<std::sync::Mutex<Vec<DiagEntry>>> =
        std::sync::Arc::new(std::sync::Mutex::new(Vec::new()));
    let entries_clone = diag_entries.clone();

    let stderr_capture = std::sync::Arc::new(std::sync::Mutex::new(String::new()));
    let stderr_clone = stderr_capture.clone();
    let stderr_handle = std::thread::spawn(move || {
        use std::io::BufRead;
        let mut buf = String::new();
        for line in std::io::BufReader::new(stderr)
            .lines()
            .map_while(Result::ok)
        {
            buf.push_str(&line);
            buf.push('\n');
        }
        if let Ok(mut s) = stderr_clone.lock() {
            *s = buf;
        }
    });

    let handle = std::thread::spawn(move || {
        for line in std::io::BufReader::new(stdout).lines() {
            let Ok(line) = line else { continue };
            if let Ok(msg) = serde_json::from_str::<serde_json::Value>(&line) {
                if let Some(reason) = msg.get("reason").and_then(|r| r.as_str()) {
                    match reason {
                        "compiler-artifact" => {
                            if let Some(package_id) = msg.get("package_id").and_then(|p| p.as_str())
                            {
                                let crate_name = extract_crate_name(package_id);
                                if let Some(target) = msg.get("target") {
                                    if target.get("kind").and_then(|k| k.as_array()).is_some_and(
                                        |k| k.iter().any(|kind| kind.as_str() == Some("lib")),
                                    ) {
                                        pb_clone.set_message(format!("compile {}", crate_name));
                                    }
                                }
                            }
                        }
                        "compiler-message" => {
                            if let Some(rendered) = msg
                                .get("message")
                                .and_then(|m| m.get("rendered"))
                                .and_then(|r| r.as_str())
                            {
                                if verbose {
                                    pb_clone.println(rendered);
                                }
                                if let Ok(mut d) = diag_clone.lock() {
                                    d.push_str(rendered);
                                    d.push('\n');
                                }
                                if !verbose {
                                    let level = msg
                                        .get("message")
                                        .and_then(|m| m.get("level"))
                                        .and_then(|l| l.as_str())
                                        .unwrap_or("")
                                        .to_string();
                                    let diag_msg = msg
                                        .get("message")
                                        .and_then(|m| m.get("message"))
                                        .and_then(|m| m.as_str())
                                        .unwrap_or("")
                                        .to_string();
                                    let location = msg.get("message").and_then(|m| {
                                        m.get("spans")
                                            .and_then(|s| s.as_array())
                                            .and_then(|a| a.first())
                                            .and_then(|span| {
                                                let file = span
                                                    .get("file_name")
                                                    .and_then(|f| f.as_str())
                                                    .unwrap_or("");
                                                let line = span
                                                    .get("line_start")
                                                    .and_then(|l| l.as_u64())
                                                    .unwrap_or(0);
                                                if file.is_empty() {
                                                    return None;
                                                }
                                                let name = std::path::Path::new(file)
                                                    .file_name()
                                                    .and_then(|n| n.to_str())
                                                    .unwrap_or(file);
                                                Some(if line > 0 {
                                                    format!("{}:{}", name, line)
                                                } else {
                                                    name.to_string()
                                                })
                                            })
                                    });
                                    if matches!(level.as_str(), "warning" | "error") {
                                        if let Ok(mut entries) = entries_clone.lock() {
                                            entries.push(DiagEntry {
                                                level,
                                                message: diag_msg,
                                                location,
                                            });
                                        }
                                    }
                                }
                            }
                        }
                        "build-script-executed" => {
                            if let Some(package_id) = msg.get("package_id").and_then(|p| p.as_str())
                            {
                                let crate_name = extract_crate_name(package_id);
                                pb_clone.set_message(format!("build script {}", crate_name));
                            }
                        }
                        "build-finished" => {}
                        _ => {}
                    }
                }
            }
        }
    });

    let status = child.wait()?;
    handle.join().ok();
    stderr_handle.join().ok();

    if !status.success() {
        if !verbose {
            let entries = diag_entries.lock().map(|e| e.clone()).unwrap_or_default();
            let n_err = entries.iter().filter(|e| e.level == "error").count();
            let n_warn = entries.iter().filter(|e| e.level == "warning").count();
            for entry in &entries {
                match &entry.location {
                    Some(loc) => {
                        crate::log_progress!("{}: {} — {}", entry.level, entry.message, loc)
                    }
                    None => crate::log_progress!("{}: {}", entry.level, entry.message),
                }
            }
            let mut parts = Vec::new();
            if n_err > 0 {
                parts.push(
                    crate::fmt_tmpl!(locale().build.diagnostic_count, n => n_err.to_string()),
                );
            }
            if n_warn > 0 {
                parts.push(crate::fmt_tmpl!(locale().build.warning_count, n => n_warn.to_string()));
            }
            if !parts.is_empty() {
                crate::log_progress!(
                    "{}",
                    crate::fmt_tmpl!(locale().build.compilation_failed, details => parts.join(", "))
                );
            }
        }
        let diag_text = diagnostics
            .lock()
            .map(|d| d.trim().to_string())
            .unwrap_or_default();
        let stderr_text = stderr_capture
            .lock()
            .map(|s| s.trim().to_string())
            .unwrap_or_default();
        let error_detail = if !diag_text.is_empty() {
            diag_text
        } else if !stderr_text.is_empty() {
            stderr_text
        } else {
            "cargo build --target wasm32-wasip2 failed".to_string()
        };
        if daemon::is_daemon() {
            let _ = daemon::append_build_log("component", false, Some(&error_detail));
        }
        return Err(crate::TairitsuPackagerError::BuildError(error_detail));
    }

    if !verbose {
        let entries = diag_entries.lock().map(|e| e.clone()).unwrap_or_default();
        let n_warn = entries.iter().filter(|e| e.level == "warning").count();
        let n_err = entries.iter().filter(|e| e.level == "error").count();
        if n_warn > 0 || n_err > 0 {
            for entry in &entries {
                match &entry.location {
                    Some(loc) => {
                        crate::log_progress!("{}: {} — {}", entry.level, entry.message, loc)
                    }
                    None => crate::log_progress!("{}: {}", entry.level, entry.message),
                }
            }
            let mut parts = Vec::new();
            if n_err > 0 {
                parts.push(
                    crate::fmt_tmpl!(locale().build.diagnostic_count, n => n_err.to_string()),
                );
            }
            if n_warn > 0 {
                parts.push(crate::fmt_tmpl!(locale().build.warning_count, n => n_warn.to_string()));
            }
            crate::log_progress!(
                "{}",
                crate::fmt_tmpl!(locale().build.compiled_with, details => parts.join(", "))
            );
        }
    }

    let workspace_root = find_workspace_root(&config.manifest_dir)?;
    let profile = if release { "release" } else { "dev-wasm" };
    let wasm_path = workspace_root
        .join("target")
        .join("wasm32-wasip2")
        .join(profile)
        .join(format!("{}.wasm", pkg_name.replace('-', "_")));

    if !wasm_path.exists() {
        return Err(crate::TairitsuPackagerError::BuildError(
            crate::fmt_tmpl!(locale().build.component_not_found, path => wasm_path.display().to_string()),
        ));
    }

    Ok(wasm_path)
}

/// Resolve the browser-glue runtime bundle (`dist/runtime.js`).
///
/// Search order:
/// 1. `TAIRITSU_RUNTIME_BUNDLE` environment variable (absolute or relative path)
/// 2. Monorepo-relative paths (for in-tree development)
/// 3. [`DEP_TAIRITSU_BROWSER_WORLDS_WIT_DIR`] auto-discovery (for crates.io users)
/// 4. `node_modules/` paths (for npm-installed `tairitsu-browser-glue`)
fn resolve_glue_runtime_bundle(manifest_dir: &std::path::Path) -> crate::Result<String> {
    if let Ok(env_path) = std::env::var("TAIRITSU_RUNTIME_BUNDLE") {
        let path = std::path::Path::new(&env_path);
        if path.exists() {
            return std::fs::read_to_string(path).map_err(|e| {
                crate::TairitsuPackagerError::BuildError(format!(
                    "Failed to read browser-glue runtime bundle from env override {}: {}",
                    path.display(),
                    e
                ))
            });
        }
        eprintln!(
            "[tairitsu] Warning: TAIRITSU_RUNTIME_BUNDLE={} does not exist, falling back to default search",
            path.display()
        );
    }

    let candidates: &[&str] = &[
        "../../../tairitsu/packages/browser-glue/dist/runtime.js",
        "../../packages/browser-glue/dist/runtime.js",
        "../packages/browser-glue/dist/runtime.js",
        "packages/browser-glue/dist/runtime.js",
        "../browser-glue/dist/runtime.js",
    ];
    for candidate in candidates {
        let path = manifest_dir.join(candidate);
        if path.exists() {
            return std::fs::read_to_string(&path).map_err(|e| {
                crate::TairitsuPackagerError::BuildError(format!(
                    "Failed to read browser-glue runtime bundle {}: {}",
                    path.display(),
                    e
                ))
            });
        }
    }

    if let Ok(bw_dir) = std::env::var("DEP_TAIRITSU_BROWSER_WORLDS_WIT_DIR") {
        let glue_path = std::path::Path::new(&bw_dir)
            .parent()
            .and_then(|p| p.parent())
            .map(|p| p.join("browser-glue/dist/runtime.js"));
        if let Some(glue_path) = glue_path {
            if glue_path.exists() {
                return std::fs::read_to_string(&glue_path).map_err(|e| {
                    crate::TairitsuPackagerError::BuildError(format!(
                        "Failed to read browser-glue runtime bundle {}: {}",
                        glue_path.display(),
                        e
                    ))
                });
            }
        }
    }

    let node_modules_candidates: &[&str] = &[
        "node_modules/tairitsu-browser-glue/dist/runtime.js",
        "node_modules/@tairitsu/browser-glue/dist/runtime.js",
        "../node_modules/tairitsu-browser-glue/dist/runtime.js",
    ];
    for candidate in node_modules_candidates {
        let path = manifest_dir.join(candidate);
        if path.exists() {
            return std::fs::read_to_string(&path).map_err(|e| {
                crate::TairitsuPackagerError::BuildError(format!(
                    "Failed to read browser-glue runtime bundle {}: {}",
                    path.display(),
                    e
                ))
            });
        }
    }

    Err(crate::TairitsuPackagerError::BuildError(
        "browser-glue runtime bundle (dist/runtime.js) not found. \
         Set TAIRITSU_RUNTIME_BUNDLE env var to the path of runtime.js, \
         or ensure browser-glue is built (run `npm run build` in packages/browser-glue)."
            .to_string(),
    ))
}

fn write_browser_glue_bundle(config: &Config, output_dir: &std::path::Path) -> crate::Result<()> {
    let glue_path = config.build.browser_glue_path.trim_start_matches('/');
    let bundle_dest = output_dir.join(glue_path);

    if let Some(parent) = bundle_dest.parent() {
        std::fs::create_dir_all(parent)?;
    }

    let bundle = resolve_glue_runtime_bundle(&config.manifest_dir)?;
    std::fs::write(&bundle_dest, &bundle)?;

    Ok(())
}

fn flatten_barrel_exports(barrel_path: &std::path::Path) -> crate::Result<()> {
    let content = std::fs::read_to_string(barrel_path).map_err(|e| {
        crate::TairitsuPackagerError::BuildError(format!(
            "Failed to read barrel {}: {}",
            barrel_path.display(),
            e
        ))
    })?;

    let mut needs_flatten = false;
    for line in content.lines() {
        let trimmed = line.trim();
        if trimmed.starts_with("export * as ")
            || (trimmed.contains("export * from") && trimmed.contains("glue/index"))
        {
            needs_flatten = true;
            break;
        }
    }

    if !needs_flatten {
        return Ok(());
    }

    let glue_dir = barrel_path.parent().map(|p| p.join("glue"));
    let Some(glue_dir) = glue_dir else {
        return Ok(());
    };

    let sym_map = build_symbol_module_map(&glue_dir);

    let mut module_syms: std::collections::BTreeMap<String, Vec<String>> =
        std::collections::BTreeMap::new();
    for (sym, mod_name) in &sym_map {
        module_syms
            .entry(mod_name.clone())
            .or_default()
            .push(sym.clone());
    }

    let mut lines = vec![
        "/**".to_string(),
        " * @tairitsu/browser-glue — flattened barrel (auto-generated)".to_string(),
        " * Combines exports from all glue submodules into named re-exports.".to_string(),
        " */".to_string(),
    ];
    for (mod_name, syms) in &module_syms {
        if syms.is_empty() {
            continue;
        }
        lines.push(format!(
            "export {{ {} }} from \"./glue/{}.js\";",
            syms.join(", "),
            mod_name
        ));
    }

    std::fs::write(barrel_path, lines.join("\n")).map_err(|e| {
        crate::TairitsuPackagerError::BuildError(format!(
            "Failed to write flattened barrel {}: {}",
            barrel_path.display(),
            e
        ))
    })?;

    crate::log_info!(
        "{}",
        crate::fmt_tmpl!(
            locale().build.flattened_barrel,
            path => barrel_path.display().to_string(),
            modules => module_syms.len().to_string(),
            symbols => sym_map.len().to_string()
        )
    );
    Ok(())
}

fn build_symbol_module_map(
    glue_dir: &std::path::Path,
) -> std::collections::HashMap<String, String> {
    let mut map: std::collections::HashMap<String, (String, bool)> =
        std::collections::HashMap::new();
    let Ok(entries) = std::fs::read_dir(glue_dir) else {
        return std::collections::HashMap::new();
    };
    for entry in entries.flatten() {
        let path = entry.path();
        if path.extension().is_none_or(|e| e != "js") {
            continue;
        }
        let Some(module_name) = path.file_stem().and_then(|s| s.to_str()) else {
            continue;
        };
        let Ok(content) = std::fs::read_to_string(&path) else {
            continue;
        };
        for line in content.lines() {
            let trimmed = line.trim();
            let export_start = trimmed
                .find("export ")
                .or_else(|| trimmed.find("export\t"))
                .unwrap_or(usize::MAX);
            if export_start == usize::MAX {
                continue;
            }
            let after_export = &trimmed[export_start + 7..];
            let prefix = if after_export.starts_with("function ") {
                "function "
            } else if after_export.starts_with("const ") {
                "const "
            } else if after_export.starts_with("let ") {
                "let "
            } else if after_export.starts_with("var ") {
                "var "
            } else if after_export.starts_with("async function ") {
                "async function "
            } else {
                continue;
            };
            if let Some(rest) = after_export.strip_prefix(prefix) {
                if let Some(sym_full) = rest.split_whitespace().next() {
                    let sym_name = sym_full
                        .split('(')
                        .next()
                        .unwrap_or(sym_full)
                        .trim()
                        .to_string();
                    if !sym_name.is_empty() {
                        let has_self = rest.contains("(self)") || rest.contains("(self,");
                        match map.get(&sym_name) {
                            Some((_other_mod, other_has_self)) => {
                                if *other_has_self && !has_self {
                                    map.insert(sym_name, (module_name.to_string(), has_self));
                                }
                            }
                            None => {
                                map.insert(sym_name, (module_name.to_string(), has_self));
                            }
                        }
                    }
                }
            }
        }
    }
    map.into_iter().map(|(k, (v, _))| (k, v)).collect()
}

fn copy_browser_glue_with_output_dir(
    config: &Config,
    output_dir: &std::path::Path,
    _pb: &ProgressBar,
) -> crate::Result<()> {
    // Also copy the full browser-glue dist so that jco-generated wrapper
    // ES module imports (e.g. `import { setProperty } from '@tairitsu/browser-glue'`)
    // can resolve. The dist contains index.js (main barrel) + glue/*.js submodules.
    // Try tairitsu workspace first (where browser-glue is built), then local.
    let glue_dist_candidates: &[&str] = &[
        "../../../tairitsu/packages/browser-glue/dist", // hikari/examples/website → tairitsu
        "../../packages/browser-glue/dist",             // tairitsu/examples/website → tairitsu
        "../packages/browser-glue/dist",                // any project examples/* → workspace root
        "packages/browser-glue/dist",                   // workspace root itself
    ];
    let mut copied = false;
    for candidate in glue_dist_candidates {
        let glue_dist = config.manifest_dir.join(candidate);
        if glue_dist.exists() {
            let dest_glue = output_dir.join("browser-glue");
            std::fs::create_dir_all(&dest_glue)?;
            for entry in walkdir::WalkDir::new(&glue_dist) {
                let entry = entry.map_err(|e| {
                    crate::TairitsuPackagerError::BuildError(format!(
                        "Failed to walk browser-glue dist: {}",
                        e
                    ))
                })?;
                let path = entry.path();
                let rel = path.strip_prefix(&glue_dist).map_err(|_| {
                    crate::TairitsuPackagerError::BuildError(
                        "Failed to compute relative path".to_string(),
                    )
                })?;
                let dest = dest_glue.join(rel);
                if path.is_dir() {
                    std::fs::create_dir_all(&dest)?;
                } else {
                    std::fs::copy(path, &dest)?;
                }
            }
            copied = true;

            let top_barrel = dest_glue.join("index.js");
            if top_barrel.exists() {
                if let Err(e) = flatten_barrel_exports(&top_barrel) {
                    crate::log_warn!("Failed to flatten top-level barrel: {}", e);
                }
            }
            break;
        }
    }
    if !copied {
        crate::log_warn!("browser-glue dist not found — jco wrapper imports may fail at runtime");
    }

    Ok(())
}

fn copy_static_public_assets_with_output_dir(
    config: &Config,
    output_dir: &std::path::Path,
) -> crate::Result<()> {
    let public_dir = config.manifest_dir.join("public");

    if public_dir.exists() {
        copy_dir_contents(&public_dir, output_dir)?;
    }

    for extra in &config.assets.extra_public_dirs {
        let extra_path = config.manifest_dir.join(extra);
        if extra_path.exists() {
            crate::log_info!(
                "{}",
                crate::fmt_tmpl!(locale().build.copying_extra_public, path => extra_path.display().to_string())
            );
            copy_dir_contents(&extra_path, output_dir)?;
        } else {
            crate::log_warn!(
                "{}",
                crate::fmt_tmpl!(locale().build.extra_public_not_found, path => extra_path.display().to_string())
            );
        }
    }

    Ok(())
}

fn copy_dir_contents(src_dir: &std::path::Path, output_dir: &std::path::Path) -> crate::Result<()> {
    for entry in walkdir::WalkDir::new(src_dir) {
        let entry = entry.map_err(|e| {
            crate::TairitsuPackagerError::BuildError(format!("Failed to walk directory: {}", e))
        })?;
        let path = entry.path();
        if path.is_dir() {
            continue;
        }
        let relative = path.strip_prefix(src_dir).map_err(|e| {
            crate::TairitsuPackagerError::BuildError(format!(
                "Failed to compute relative path: {}",
                e
            ))
        })?;
        let dest = output_dir.join(relative);
        if let Some(parent) = dest.parent() {
            std::fs::create_dir_all(parent)?;
        }
        std::fs::copy(path, dest)?;
    }
    Ok(())
}

