fn prepare_component_wrapper_fallback(
    config: &Config,
    component_wasm_path: &std::path::Path,
    output_dir: &std::path::Path,
    pb: &ProgressBar,
) -> crate::Result<()> {
    write_component_wrapper_loader(config, component_wasm_path, output_dir)?;

    if !try_generate_component_wrapper(config, component_wasm_path, output_dir, pb)? {
        return Err(crate::TairitsuPackagerError::BuildError(
            "Component wrapper transpile failed. \
             Install jco: npm install -g @bytecodealliance/jco\n\
             The dev server requires the jco-transpiled wrapper to serve WASM components."
                .to_string(),
        ));
    }

    let local_shim_available = bundle_local_wasi_shim(output_dir, pb)?;
    rewrite_wrapper_imports(output_dir, local_shim_available)?;

    Ok(())
}

const WASI_SHIM_MODULES: &[&str] = &["io", "cli", "random", "clocks", "filesystem", "sockets"];

fn bundle_local_wasi_shim(output_dir: &std::path::Path, pb: &ProgressBar) -> crate::Result<bool> {
    let shim_dir = output_dir.join("wasi-shim");
    if shim_dir.exists() {
        let marker = shim_dir.join(".tairitsu-bundled");
        if marker.exists() {
            return Ok(true);
        }
    }

    let node = if cfg!(windows) { "node.exe" } else { "node" };
    let script = include_str!("bundle-wasi-shim.js");

    let temp_script = output_dir.join("_wasi_shim_bundle.js");
    std::fs::write(&temp_script, script)?;

    let result = std::process::Command::new(node)
        .arg(&temp_script)
        .arg(shim_dir.to_str().unwrap_or("."))
        .output();

    let _ = std::fs::remove_file(&temp_script);

    match result {
        Ok(output) if output.status.success() => {
            let count = WASI_SHIM_MODULES.len();
            pb.println(format!(
                "✓  Bundled {} WASI preview2-shim modules locally",
                count
            ));
            Ok(true)
        }
        Ok(_) => {
            pb.println(
                "⚠  Could not bundle WASI shim locally (npm unavailable?). Falling back to CDN.",
            );
            Ok(false)
        }
        Err(_) => {
            pb.println(
                "⚠  Could not bundle WASI shim locally (node not found?). Falling back to CDN.",
            );
            Ok(false)
        }
    }
}

fn rewrite_wrapper_imports(
    output_dir: &std::path::Path,
    local_shim_available: bool,
) -> crate::Result<()> {
    let wrapper_dir = output_dir.join("component-wrapper");
    if !wrapper_dir.exists() {
        return Ok(());
    }

    let local_prefix = if local_shim_available {
        "'/wasi-shim/"
    } else {
        "'https://esm.sh/@bytecodealliance/preview2-shim/"
    };
    let local_prefix_dq = if local_shim_available {
        "\"/wasi-shim/"
    } else {
        "\"https://esm.sh/@bytecodealliance/preview2-shim/"
    };

    for entry in std::fs::read_dir(&wrapper_dir)? {
        let entry = entry?;
        let path = entry.path();
        if !path.is_file() {
            continue;
        }
        if path.extension().and_then(|s| s.to_str()) != Some("js") {
            continue;
        }

        let content = std::fs::read_to_string(&path)?;
        let mut patched = content
            .replace("'@bytecodealliance/preview2-shim/", local_prefix)
            .replace("\"@bytecodealliance/preview2-shim/", local_prefix_dq);

        if local_shim_available {
            patched = patched
                .replace("'/wasi-shim/io'", "'/wasi-shim/io.js'")
                .replace("'/wasi-shim/cli'", "'/wasi-shim/cli.js'")
                .replace("'/wasi-shim/random'", "'/wasi-shim/random.js'")
                .replace("'/wasi-shim/clocks'", "'/wasi-shim/clocks.js'")
                .replace("'/wasi-shim/filesystem'", "'/wasi-shim/filesystem.js'")
                .replace("'/wasi-shim/sockets'", "'/wasi-shim/sockets.js'")
                .replace("\"/wasi-shim/io\"", "\"/wasi-shim/io.js\"")
                .replace("\"/wasi-shim/cli\"", "\"/wasi-shim/cli.js\"")
                .replace("\"/wasi-shim/random\"", "\"/wasi-shim/random.js\"")
                .replace("\"/wasi-shim/clocks\"", "\"/wasi-shim/clocks.js\"")
                .replace("\"/wasi-shim/filesystem\"", "\"/wasi-shim/filesystem.js\"")
                .replace("\"/wasi-shim/sockets\"", "\"/wasi-shim/sockets.js\"");
        }

        if patched != content {
            std::fs::write(&path, patched)?;
        }
    }

    Ok(())
}

fn write_component_wrapper_loader(
    _config: &Config,
    component_wasm_path: &std::path::Path,
    output_dir: &std::path::Path,
) -> crate::Result<()> {
    let wasm_stem = component_wasm_path
        .file_stem()
        .and_then(|s| s.to_str())
        .ok_or_else(|| {
            crate::TairitsuPackagerError::BuildError(
                "Failed to derive wasm file stem for wrapper loader".to_string(),
            )
        })?;

    let loader = include_str!(concat!(
        env!("CARGO_MANIFEST_DIR"),
        "/src/wasm/component-wrapper-loader.template.js"
    ))
    .replace("__WASM_STEM__", wasm_stem);

    let loader_path = output_dir.join("component-wrapper-loader.js");
    std::fs::write(loader_path, loader)?;
    Ok(())
}

fn try_generate_component_wrapper(
    _config: &Config,
    component_wasm_path: &std::path::Path,
    output_dir: &std::path::Path,
    pb: &ProgressBar,
) -> crate::Result<bool> {
    let wrapper_dir = output_dir.join("component-wrapper");
    std::fs::create_dir_all(&wrapper_dir)?;

    let wasm_path_for_cmd = std::fs::canonicalize(component_wasm_path)
        .unwrap_or_else(|_| component_wasm_path.to_path_buf());
    let wrapper_dir_for_cmd = std::fs::canonicalize(&wrapper_dir).unwrap_or(wrapper_dir.clone());
    let wasm_path_hint = wasm_path_for_cmd
        .display()
        .to_string()
        .trim_start_matches(r"\\?\")
        .to_string();
    let wrapper_dir_hint = wrapper_dir_for_cmd
        .display()
        .to_string()
        .trim_start_matches(r"\\?\")
        .to_string();

    let wasm_stem = component_wasm_path
        .file_stem()
        .and_then(|s| s.to_str())
        .ok_or_else(|| {
            crate::TairitsuPackagerError::BuildError(
                "Failed to derive wasm file stem for wrapper detection".to_string(),
            )
        })?;

    let wrapper_main = if wrapper_dir.join("index.js").exists() {
        Some(wrapper_dir.join("index.js"))
    } else {
        let named = wrapper_dir.join(format!("{}.js", wasm_stem));
        if named.exists() {
            Some(named)
        } else {
            None
        }
    };

    if let Some(main) = wrapper_main {
        let wrapper_mtime = std::fs::metadata(&main)?.modified().ok();
        let wasm_mtime = std::fs::metadata(component_wasm_path)?.modified().ok();
        if let (Some(w), Some(c)) = (wrapper_mtime, wasm_mtime) {
            if w >= c {
                return Ok(true);
            }
        }
    }

    #[allow(unused_mut)]
    let mut attempts: Vec<(&str, Vec<String>)> = vec![
        (
            "jco",
            vec![
                "transpile".to_string(),
                wasm_path_for_cmd.display().to_string(),
                "-o".to_string(),
                wrapper_dir_for_cmd.display().to_string(),
            ],
        ),
        (
            "npx",
            vec![
                "--yes".to_string(),
                "@bytecodealliance/jco".to_string(),
                "transpile".to_string(),
                wasm_path_for_cmd.display().to_string(),
                "-o".to_string(),
                wrapper_dir_for_cmd.display().to_string(),
            ],
        ),
    ];

    #[cfg(windows)]
    {
        attempts.push((
            "npx.cmd",
            vec![
                "--yes".to_string(),
                "@bytecodealliance/jco".to_string(),
                "transpile".to_string(),
                wasm_path_for_cmd.display().to_string(),
                "-o".to_string(),
                wrapper_dir_for_cmd.display().to_string(),
            ],
        ));
    }

    let mut missing_commands = Vec::new();

    let re_import =
        regex::Regex::new(r"(?:import\s*\{([^}]+)\}\s*from\s*'@tairitsu-glue/([^']*)'\s*;?)")
            .unwrap();
    let re_import_d =
        regex::Regex::new(r#"(?:import\s*\{([^}]+)\}\s*from\s*"@tairitsu-glue/([^"]*)"\s*;?)"#)
            .unwrap();

    for (bin, args) in attempts {
        let command_preview = format!("{} {}", bin, args.join(" "));
        match std::process::Command::new(bin).args(&args).output() {
            Ok(output) if output.status.success() => {
                let has_index = wrapper_dir.join("index.js").exists();
                let has_named = wrapper_dir.join(format!("{}.js", wasm_stem)).exists();
                if has_index || has_named {
                    // Post-process the generated JS to replace tairitsu-browser:full/ imports
                    // with @tairitsu-glue/ imports which are valid bare module specifiers
                    let js_files = vec![
                        wrapper_dir.join("index.js"),
                        wrapper_dir.join(format!("{}.js", wasm_stem)),
                    ];
                    for js_file in js_files {
                        if js_file.exists() {
                            if let Ok(mut content) = std::fs::read_to_string(&js_file) {
                                let original = content.clone();
                                content = content.replace(
                                    "from 'tairitsu-browser:full/",
                                    "from '@tairitsu-glue/",
                                );
                                content = content.replace(
                                    "from \"tairitsu-browser:full/",
                                    "from \"@tairitsu-glue/",
                                );

                                let _import_map_interfaces: std::collections::HashSet<&str> =
                                    [].into_iter().collect();

                                content = re_import
                                    .replace_all(&content, |caps: &regex::Captures| {
                                        let _m0 = caps.get(0).map(|m| m.as_str()).unwrap_or("");
                                        let m1 = caps.get(1).map(|m| m.as_str()).unwrap_or("");
                                        let m2 = caps.get(2).map(|m| m.as_str()).unwrap_or("");
                                        if m1.is_empty() {
                                            return String::new();
                                        }
                                        let _iface_name = m2.split('/').next().unwrap_or("");

                                        // Split symbols: import-map-available vs needs-glue-fallback
                                        let mut map_syms = Vec::new();
                                        for sym in m1.trim().split(',') {
                                            let s = sym.trim();
                                            if s.is_empty() {
                                                continue;
                                            }
                                            // All interfaces now provided by __tairitsu_glue__.js import map
                                            map_syms.push(s.to_string());
                                        }

                                        let mut parts = Vec::new();
                                        if !map_syms.is_empty() {
                                            parts.push(format!(
                                                "import {{ {} }} from '@tairitsu-glue/{}';",
                                                map_syms.join(", "),
                                                m2
                                            ));
                                        }
                                        // Resolve each symbol to its own module
                                        parts.join("\n  ")
                                    })
                                    .to_string();
                                content = re_import_d
                                    .replace_all(&content, |caps: &regex::Captures| {
                                        let _m0 = caps.get(0).map(|m| m.as_str()).unwrap_or("");
                                        let m1 = caps.get(1).map(|m| m.as_str()).unwrap_or("");
                                        let m2 = caps.get(2).map(|m| m.as_str()).unwrap_or("");
                                        if m1.is_empty() {
                                            return String::new();
                                        }
                                        let _iface_name = m2.split('/').next().unwrap_or("");

                                        let mut map_syms = Vec::new();
                                        for sym in m1.trim().split(',') {
                                            let s = sym.trim();
                                            if s.is_empty() {
                                                continue;
                                            }
                                            map_syms.push(s.to_string());
                                        }

                                        let mut parts = Vec::new();
                                        if !map_syms.is_empty() {
                                            parts.push(format!(
                                                "import {{ {} }} from \"@tairitsu-glue/{}\";",
                                                map_syms.join(", "),
                                                m2
                                            ));
                                        }
                                        parts.join("\n  ")
                                    })
                                    .to_string();

                                if content != original {
                                    std::fs::write(&js_file, content)?;
                                }
                            }
                        }

                        // Patch preview2-shim resource instanceof checks that fail
                        // due to cross-module class identity issues (jco + preview2-shim).
                        // The getStdout/getStderr/subscribe calls return valid objects but
                        // from a different module instance than the expected class.
                        if let Ok(mut content) = std::fs::read_to_string(&js_file) {
                            let original = content.clone();
                            let patterns = [
                                ("OutputStream", "\"OutputStream\""),
                                ("InputStream", "\"InputStream\""),
                                ("Pollable", "\"Pollable\""),
                                ("TerminalOutput", "\"TerminalOutput\""),
                                ("TerminalInput", "\"TerminalInput\""),
                            ];
                            for (class_name, label) in patterns {
                                let check = format!(
                                    "if (!(ret instanceof {})) {{\n      throw new TypeError('Resource error: Not a valid {} resource.');\n    }}",
                                    class_name, label
                                );
                                let catch_check = format!(
                                    "if (!(e instanceof {})) {{\n      throw new TypeError('Resource error: Not a valid {} resource.');\n    }}",
                                    class_name, label
                                );
                                let comment = format!(
                                    "/* {} instanceof check patched for browser compat */",
                                    class_name
                                );
                                content = content.replace(&check, &comment);
                                content = content.replace(&catch_check, &comment);
                            }
                            if content != original {
                                std::fs::write(&js_file, content)?;
                            }
                        }
                    }
                    return Ok(true);
                }
                pb.println(format!(
                    "⚠  Wrapper transpile command succeeded but no JS wrapper entry was found.\n   Command: {}",
                    command_preview
                ));
                continue;
            }
            Ok(output) => {
                let stderr = String::from_utf8_lossy(&output.stderr).trim().to_string();
                let stdout = String::from_utf8_lossy(&output.stdout).trim().to_string();
                let detail = if !stderr.is_empty() {
                    stderr
                } else if !stdout.is_empty() {
                    stdout
                } else {
                    "(no output)".to_string()
                };
                let detail_preview: String = detail.chars().take(400).collect();

                pb.println(format!(
                    "⚠  Wrapper transpile command failed.\n   Command: {}\n   Exit: {}\n   Detail: {}",
                    command_preview,
                    output.status,
                    detail_preview
                ));
                continue;
            }
            Err(err) if err.kind() == std::io::ErrorKind::NotFound => {
                missing_commands.push(bin.to_string());
                // Don't print per-command not found - we'll summarize at the end
                continue;
            }
            Err(err) => {
                pb.println(format!(
                    "⚠  Failed to execute wrapper transpile command.\n   Command: {}\n   Error: {}",
                    command_preview, err
                ));
                continue;
            }
        }
    }

    // Only print one summary message for missing commands
    if !missing_commands.is_empty() {
        missing_commands.sort();
        missing_commands.dedup();
        pb.println(format!(
            "⚠  Wrapper transpile command not found: '{}'.\n   \
             Attempted: jco transpile {} -o {}",
            missing_commands.join(", "),
            wasm_path_hint,
            wrapper_dir_hint
        ));
    }

    Ok(false)
}

fn collect_css_output_paths(scss_config: &crate::config::ScssConfig) -> Vec<String> {
    let mut paths = Vec::new();

    if !scss_config.entries.is_empty() {
        for entry in &scss_config.entries {
            paths.push(entry.output.clone());
        }
    } else if scss_config.entry.is_some() {
        paths.push(scss_config.output.clone());
    } else {
        paths.push("styles.css".to_string());
    }

    paths
}

fn generate_component_html_with_output_dir(
    config: &Config,
    output_dir: &std::path::Path,
) -> crate::Result<()> {
    let pkg_name = &config.package.name;
    let wasm_file = format!("{}.wasm", pkg_name.replace('-', "_"));
    // Build-time version stamp so browsers never serve stale cached JS/WASM
    // modules even if `Cache-Control: no-store` is bypassed by a caching proxy.
    let v = std::time::SystemTime::now()
        .duration_since(std::time::UNIX_EPOCH)
        .map(|d| d.as_secs())
        .unwrap_or(0);

    let title = config.html.title.as_deref().unwrap_or(pkg_name.as_str());
    let favicon = config
        .html
        .favicon
        .as_deref()
        .map(|f| f.strip_prefix("public/").unwrap_or(f).replace('\\', "/"));
    let favicon_link = favicon
        .as_deref()
        .map(|href| format!("<link rel=\"icon\" href=\"./{}\">", href))
        .unwrap_or_default();

    let glue_bundle_path = &config.build.browser_glue_path;

    let mut ctx = tera::Context::new();
    ctx.insert("lang", &config.html.lang);
    ctx.insert("charset", &config.html.charset);
    ctx.insert("viewport", &config.html.viewport);
    ctx.insert("title", &title);
    ctx.insert("favicon", &favicon_link);
    ctx.insert("head", &config.html.head);
    ctx.insert("body_class", &config.html.body_class);
    ctx.insert("wasm_file", &wasm_file);
    ctx.insert("glue_bundle", glue_bundle_path);
    ctx.insert("v", &v.to_string());

    let mut tera = tera::Tera::default();
    tera.add_raw_template("index.html", include_str!("index.html.template"))
        .map_err(|e| {
            crate::TairitsuPackagerError::BuildError(format!("Template parse error: {e}"))
        })?;
    let html = tera.render("index.html", &ctx).map_err(|e| {
        crate::TairitsuPackagerError::BuildError(format!("Template render error: {e}"))
    })?;

    let mut html = html;

    if config.html.inline_css {
        let injector = crate::styles::StyleInjector::new();
        let css_paths = collect_css_output_paths(&config.scss);

        for css_path in &css_paths {
            let full_path = output_dir.join(css_path);
            match std::fs::read_to_string(&full_path) {
                Ok(css_content) => {
                    html = injector.inject_style_into_html(&html, &css_content)?;
                    crate::log_info!(
                        "{}",
                        crate::fmt_tmpl!(locale().build.inlined_css, path => css_path.to_string())
                    );
                }
                Err(e) => {
                    crate::log_warn!(
                        "CSS file not found for inline injection: {} ({})",
                        css_path,
                        e
                    );
                }
            }
        }
    }

    let html_path = output_dir.join("index.html");
    std::fs::write(&html_path, &html)?;

    generate_route_html_files(config, output_dir, &html)?;

    Ok(())
}

fn generate_route_html_files(
    config: &Config,
    output_dir: &std::path::Path,
    base_html: &str,
) -> crate::Result<()> {
    use crate::config::DiscoveredRoute;

    let routes = config.discovered_routes();
    if routes.is_empty() {
        return Ok(());
    }

    let non_root_routes: Vec<&DiscoveredRoute> = routes.iter().filter(|r| !r.is_root()).collect();

    if non_root_routes.is_empty() {
        return Ok(());
    }

    let route_preload_script = |route_path: &str| -> String {
        format!(
            "<script>if(history.replaceState){{history.replaceState(null,null,{path})}}</script>",
            path =
                serde_json::to_string(route_path).unwrap_or_else(|_| format!("'{}'", route_path))
        )
    };

    for route in &non_root_routes {
        let fs_route = route.fs_path();
        let route_dir = output_dir.join(fs_route);

        std::fs::create_dir_all(&route_dir).map_err(|e| {
            crate::TairitsuPackagerError::BuildError(format!(
                "Failed to create route directory {}: {}",
                route_dir.display(),
                e
            ))
        })?;

        let preload = route_preload_script(&route.path);
        let route_html = base_html.replace(
            "<div id=\"app\">Loading…</div>",
            &format!("<div id=\"app\" style=\"display:flex;align-items:center;justify-content:center;min-height:100vh;font-family:system-ui,sans-serif;color:rgba(255,255,255,.7)\"><div>Loading…</div></div>\n    {}", preload),
        );

        let route_file = route_dir.join("index.html");
        std::fs::write(&route_file, &route_html).map_err(|e| {
            crate::TairitsuPackagerError::BuildError(format!(
                "Failed to write route HTML {}: {}",
                route_file.display(),
                e
            ))
        })?;
    }

    crate::log_info!(
        "{}",
        crate::fmt_tmpl!(locale().build.generated_routes, n => non_root_routes.len().to_string())
    );

    Ok(())
}

/// Middleware that prevents browsers from caching JS/WASM/HTML assets in dev mode.
/// Without this, browsers serve stale cached modules even after a file changes.
#[cfg(feature = "dev-server")]
async fn no_cache_headers(
    request: axum::extract::Request,
    next: axum::middleware::Next,
) -> axum::response::Response {
    let path = request.uri().path().to_owned();
    let mut response = next.run(request).await;

    let should_bust = path == "/"
        || path.ends_with(".html")
        || path.ends_with(".js")
        || path.ends_with(".mjs")
        || path.ends_with(".wasm")
        || path.ends_with(".map");

    if should_bust {
        response.headers_mut().insert(
            axum::http::header::CACHE_CONTROL,
            axum::http::HeaderValue::from_static("no-store"),
        );
    }
    response
}

/// Returns true if the path requests a static asset (JS, WASM, CSS, etc.)
/// that should return 404 when missing, NOT the SPA fallback HTML.
#[cfg(feature = "dev-server")]
fn is_asset_request(path: &str) -> bool {
    const ASSET_EXTENSIONS: &[&str] = &[
        ".js", ".mjs", ".cjs", ".wasm", ".css", ".map", ".svg", ".png", ".jpg", ".jpeg", ".gif",
        ".ico", ".webp", ".woff", ".woff2", ".ttf", ".otf", ".eot",
    ];
    ASSET_EXTENSIONS.iter().any(|ext| path.ends_with(ext))
}

/// Middleware that returns a proper 404 for asset file requests (.js, .wasm, etc.)
/// when the file does not exist on disk.  Without this, the SPA fallback handler
/// would serve index.html (text/html) for missing JS files, causing:
///   "Failed to load module script: Expected a JavaScript module but got text/html"
#[cfg(feature = "dev-server")]
async fn reject_missing_assets(
    axum::extract::State(dist): axum::extract::State<std::sync::Arc<std::path::PathBuf>>,
    request: axum::extract::Request,
    next: axum::middleware::Next,
) -> axum::response::Response {
    use axum::response::IntoResponse;
    let path = request.uri().path();
    let is_get_or_head = *request.method() == axum::http::Method::GET
        || *request.method() == axum::http::Method::HEAD;
    if is_asset_request(path) && is_get_or_head {
        let clean = path.trim_start_matches('/');
        let file_path = dist.join(clean);
        if !file_path.exists() {
            return (
                axum::http::StatusCode::NOT_FOUND,
                format!("404 Not Found: {}", path),
            )
                .into_response();
        }
    }
    next.run(request).await
}

#[cfg(feature = "dev-server")]
#[allow(clippy::too_many_arguments)]
pub async fn dev_server(
    config: &Config,
    port: u16,
    open: bool,
    watch: bool,
    force: bool,
    verbose: bool,
    debug: bool,
    debug_port: Option<u16>,
    clean: bool,
) -> crate::Result<()> {
    use axum::response::Html;
    use axum::routing::get;
    use axum::{middleware, Router};
    use tower_http::services::ServeDir;

    if watch {
        crate::log_info!("{}", locale().build.dev_server_watch);
    } else {
        crate::log_info!("{}", locale().build.dev_server);
    }

    if !crate::daemon::is_daemon() && crate::daemon::is_daemon_running() {
        let pid = crate::daemon::read_pid().unwrap_or(0);
        crate::log_warn!(
            "{}",
            crate::fmt_tmpl!(locale().build.daemon_already_running, pid => pid.to_string())
        );
        crate::log_info!("{}", locale().build.daemon_attach_hint);
    }

    {
        let probe = std::net::TcpListener::bind(("127.0.0.1", port));
        if let Err(ref err) = probe {
            if err.kind() == std::io::ErrorKind::AddrInUse {
                let owner = crate::daemon::port_owner_info(port);
                let our_pid = crate::daemon::read_pid().unwrap_or(0);

                if let Some(ref info) = owner {
                    if info.pid == our_pid && !crate::daemon::is_daemon() {
                        return Err(crate::TairitsuPackagerError::BuildError(
                            crate::fmt_tmpl!(locale().dev.port_in_use_daemon, port => port.to_string(), pid => info.pid.to_string()),
                        ));
                    }

                    let is_tairitsu = info
                        .exe_path
                        .as_ref()
                        .map(|exe| {
                            let exe_str = exe.to_string_lossy().to_lowercase();
                            exe_str.contains("tairitsu")
                        })
                        .unwrap_or(false);

                    if is_tairitsu {
                        if !force {
                            let mut msg = crate::fmt_tmpl!(locale().dev.port_in_use_other_daemon, port => port.to_string(), pid => info.pid.to_string());
                            if let Some(ref exe) = info.exe_path {
                                msg.push_str(&format!("\n  Executable: {}", exe.display()));
                            }
                            msg.push_str(&format!("\n  {}", locale().dev.port_use_force));
                            return Err(crate::TairitsuPackagerError::BuildError(msg));
                        }

                        crate::log_warn!(
                            "{}",
                            crate::fmt_tmpl!(
                                locale().build.force_killing_daemon,
                                pid => info.pid.to_string(),
                                port => port.to_string()
                            )
                        );
                        crate::daemon::kill_process_by_pid(info.pid);
                        std::thread::sleep(std::time::Duration::from_millis(500));
                    }
                }

                let probe2 = std::net::TcpListener::bind(("127.0.0.1", port));
                if let Err(ref err2) = probe2 {
                    if err2.kind() == std::io::ErrorKind::AddrInUse {
                        let mut msg = crate::fmt_tmpl!(locale().dev.port_in_use_other, port => port.to_string());
                        if let Some(info) = owner {
                            msg.push_str(&format!(" (PID {})", info.pid));
                            if let Some(exe) = info.exe_path {
                                msg.push_str(&format!("\n  Executable: {}", exe.display()));
                            }
                        }
                        msg.push_str(&format!("\n  {}", locale().dev.port_change_hint));
                        return Err(crate::TairitsuPackagerError::BuildError(msg));
                    }
                }
                drop(probe2);
            }
        }
        drop(probe);
    }

    let (reload_tx, _) = tokio::sync::broadcast::channel::<()>(8);

    if clean {
        let dist_dir = if config.build.output_dir.is_relative() {
            config.manifest_dir.join(&config.build.output_dir)
        } else {
            config.build.output_dir.clone()
        };
        if dist_dir.exists() {
            crate::log_info!("Cleaning output directory: {}", dist_dir.display());
            let _ = std::fs::remove_dir_all(&dist_dir);
        }
    }

    let initial_started = Instant::now();
    crate::log_progress!(
        "{}",
        crate::fmt_tmpl!(locale().build.building_component, name => config.package.name.clone())
    );
    match config.build.target.as_str() {
        "component" => build_component(config, false, None, verbose).inspect_err(|e| {
            if crate::daemon::is_daemon() {
                let _ = crate::daemon::signal_failed(&e.to_string());
            }
        })?,
        other => {
            let err = crate::TairitsuPackagerError::BuildError(
                crate::fmt_tmpl!(locale().build.unknown_build_target, target => &other),
            );
            if crate::daemon::is_daemon() {
                let _ = crate::daemon::signal_failed(&err.to_string());
            }
            return Err(err);
        }
    }
    let initial_elapsed = initial_started.elapsed();

    let dist_dir = if config.build.output_dir.is_relative() {
        config.manifest_dir.join(&config.build.output_dir)
    } else {
        config.build.output_dir.clone()
    };

    let wasm_file = dist_dir.join(format!("{}.wasm", config.package.name.replace('-', "_")));
    let pkg_size_str = match std::fs::metadata(&wasm_file) {
        Ok(meta) => {
            let bytes = meta.len();
            if bytes >= 1_048_576 {
                format!("{:.1} MB", bytes as f64 / 1_048_576.0)
            } else {
                format!("{:.1} KB", bytes as f64 / 1024.0)
            }
        }
        Err(_) => String::from("unknown"),
    };

    crate::log_ok!(
        "{}",
        crate::fmt_tmpl!(locale().build.initial_build_ok, elapsed => format!("{:.1?}", initial_elapsed))
    );
    crate::log_info!(
        "{}",
        crate::fmt_tmpl!(locale().build.workspace_label, dir => config.manifest_dir.display().to_string())
    );
    crate::log_info!(
        "{}",
        crate::fmt_tmpl!(locale().build.package_label, name => config.package.name.clone(), version => config.package.version.clone())
    );
    crate::log_info!(
        "{}",
        crate::fmt_tmpl!(locale().build.output_label, dir => dist_dir.display().to_string(), size => pkg_size_str)
    );

    let dist_for_index = dist_dir.clone();
    let pkg_name = config.package.name.clone();
    let dist_for_spa = dist_for_index.clone();
    let spa_pkg_name = pkg_name.clone();

    let discovered = config.discovered_routes();
    let known_route_paths: Vec<String> = discovered
        .iter()
        .filter(|r| !r.is_root())
        .map(|r| r.path.clone())
        .collect();

    let spa_fallback = Router::new().fallback(get(move |req: axum::extract::Request| {
        let dist = dist_for_spa.clone();
        let pkg = spa_pkg_name.clone();
        let routes = known_route_paths.clone();
        async move {
            let path = req.uri().path().trim_end_matches('/');

            let route_file = if !path.is_empty()
                && routes
                    .iter()
                    .any(|r| r == path || format!("{}/", r) == *path)
            {
                let clean_path = path.strip_prefix('/').unwrap_or(path);
                Some(dist.join(clean_path).join("index.html"))
            } else {
                None
            };

            let content = match route_file {
                Some(ref fp) if fp.exists() => std::fs::read_to_string(fp),
                _ => Err(std::io::Error::new(
                    std::io::ErrorKind::NotFound,
                    "no route file",
                )),
            }
            .or_else(|_| std::fs::read_to_string(dist.join("index.html")))
            .unwrap_or_else(|_| {
                format!(
                    "<!DOCTYPE html><html><head><title>{}</title></head>\
                         <body><div id=\"app\">Loading…</div></body></html>",
                    pkg
                )
            });
            Html(content)
        }
    }));
    let reload_tx_for_route = reload_tx.clone();
    let dist_state = std::sync::Arc::new(dist_dir.clone());

    let app = Router::new()
        .route(
            "/__tairitsu_reload",
            get(move || {
                let tx = reload_tx_for_route.clone();
                async move {
                    use axum::response::sse::{Event, Sse};
                    use axum::response::IntoResponse;
                    use futures::stream::StreamExt;
                    let receiver = tx.subscribe();
                    let stream =
                        tokio_stream::wrappers::BroadcastStream::new(receiver).map(|result| {
                            match result {
                                Ok(()) => Ok::<_, std::io::Error>(Event::default().data("reload")),
                                Err(_) => Err(std::io::Error::other("lagged")),
                            }
                        });
                    Sse::new(stream)
                        .keep_alive(
                            axum::response::sse::KeepAlive::new()
                                .interval(Duration::from_secs(15))
                                .text("ping"),
                        )
                        .into_response()
                }
            }),
        )
        .route(
            "/",
            get(move || {
                let dist = dist_for_index.clone();
                let pkg = pkg_name.clone();
                async move {
                    let content =
                        std::fs::read_to_string(dist.join("index.html")).unwrap_or_else(|_| {
                            format!(
                                "<!DOCTYPE html><html><head><title>{}</title></head>\
                                 <body><div id=\"app\">Loading…</div></body></html>",
                                pkg
                            )
                        });
                    Html(content)
                }
            }),
        );

    let app = app
        .fallback_service(ServeDir::new(dist_dir).fallback(spa_fallback))
        .layer(middleware::from_fn_with_state(
            dist_state,
            reject_missing_assets,
        ))
        .layer(middleware::from_fn(no_cache_headers));

    let (listener, actual_port) = bind_listener_with_fallback(port).await?;

    let effective_debug_port = debug_port.unwrap_or(actual_port + 1);
    if debug {
        let cfg = config.clone();
        tokio::spawn(async move {
            if let Err(e) =
                crate::debug::start_debug_server(&cfg, actual_port, effective_debug_port).await
            {
                crate::log_warn!("Debug API server error: {}", e);
            }
        });
        crate::log_info!(
            "Debug API enabled → http://localhost:{}",
            effective_debug_port
        );
    }

    if crate::daemon::is_daemon() {
        #[cfg(feature = "debug-browser")]
        let dp = Some(effective_debug_port);
        #[cfg(not(feature = "debug-browser"))]
        let dp = None;
        let _ = crate::daemon::signal_ready(actual_port, dp);
        crate::daemon::register_cleanup_on_exit().await;
    }

    let mut last_build_line = format_last_build_line(true, initial_elapsed, None);

    let port_switched = if actual_port != port {
        Some(
            locale()
                .dev
                .port_switched
                .replace("{from}", &port.to_string())
                .replace("{to}", &actual_port.to_string()),
        )
    } else {
        None
    };

    print_status_panel(
        actual_port,
        &config.build.output_dir,
        Some(&last_build_line),
        port_switched.as_deref(),
    );

    if open || config.dev.open_browser {
        let url = format!("http://localhost:{}", actual_port);
        match webbrowser::open(&url) {
            Ok(_) => crate::log_ok!("{}", locale().dev.opening_browser),
            Err(e) => crate::log_warn!("{}: {}", locale().dev.open_browser_failed, e),
        }
    }

    if watch {
        // Run the HTTP server as a background task; this task runs the watch loop.
        tokio::spawn(async move {
            axum::serve(listener, app).await.ok();
        });
        run_watch_loop(
            config,
            actual_port,
            &config.build.output_dir,
            &mut last_build_line,
            reload_tx,
            verbose,
        )
        .await?;
    } else {
        crate::log_info!("{}", locale().dev.press_ctrl_c_to_stop);
        let shutdown = async {
            #[cfg(unix)]
            {
                use tokio::signal::unix::{signal, SignalKind};
                let mut sigterm = signal(SignalKind::terminate()).unwrap();
                tokio::select! {
                    _ = tokio::signal::ctrl_c() => {},
                    _ = sigterm.recv() => {},
                }
            }
            #[cfg(not(unix))]
            {
                tokio::signal::ctrl_c().await.ok();
            }
        };
        axum::serve(listener, app)
            .with_graceful_shutdown(shutdown)
            .await?;
    }

    Ok(())
}

