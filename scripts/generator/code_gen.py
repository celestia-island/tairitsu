#!/usr/bin/env python3
"""TypeScript code generation and rendering logic."""

from collections import Counter
from datetime import datetime
from typing import List

from wit_parser import kebab_to_camel, kebab_to_pascal

from .config import (
    GLOBAL_SINGLETONS,
    CUSTOM_TYPE_DEFINITIONS,
    PARAMETER_BIGINT_TO_NUMBER as PARAMETER_BIGINT_TO_NUMBER_ALIAS,
    PARAMETER_HANDLE_MAPPING,
    HANDLE_RETURNING_FUNCTIONS,
    STATIC_METHOD_NEEDS_TYPE_ASSERTION,
    ENUM_PROPERTIES,
    ENUM_VALUE_MAPPINGS,
    ENUM_SETTER_PROPERTIES,
    NUMBER_TO_BIGINT_PROPERTIES,
    BOOLEAN_TO_BIGINT_PROPERTIES,
    EVENT_HANDLER_PROPERTIES,
    PROPERTIES_NEEDING_TYPE_ASSERTION,
    SYNTHETIC_HANDLE_TYPES,
    correct_type_casing,
    READONLY_ARRAY_PROPERTIES,
    GETTER_RETURN_COALESCING,
    GETTER_HANDLE_NON_NULL_ASSERTION,
    HANDLE_RETURNING_ARRAY_PROPERTIES,
    PARAMS_TO_SKIP,
    SETTER_METHOD_NAMES,
)
from .models import (
    GeneratedDomain, GeneratedInterface, GeneratedFunction,
)


class CodeGenerator:
    """Generates TypeScript code from parsed WIT structures."""

    def _get_handle_info(self, target_iface: str) -> tuple:
        """Get handle variable name and pascal name for a target interface.
        
        Returns (handle_var, handle_pascal) tuple.
        """
        if target_iface in SYNTHETIC_HANDLE_TYPES:
            _, handle_var, handle_pascal = SYNTHETIC_HANDLE_TYPES[target_iface]
            return handle_var, handle_pascal
        else:
            handle_pascal = correct_type_casing(kebab_to_pascal(target_iface))
            handle_var = kebab_to_camel(target_iface + "Handles")
            return handle_var, handle_pascal

    def render_module(self, domain: GeneratedDomain, source_file: str) -> str:
        """Render TypeScript module for a domain using simple string templating."""

        lines: List[str] = []

        lines.append("/**")
        lines.append(f" * {domain.name} glue — implements the `tairitsu-browser:{domain.name}` WIT import interfaces.")
        lines.append(" *")
        lines.append(f" * Auto-generated from: {source_file}")
        lines.append(" * Generated by: scripts/generate_browser_glue.py")
        lines.append(" *")
        lines.append(" * DO NOT EDIT MANUALLY - regenerate with: just glue-gen")
        lines.append(" *")
        lines.append(" * All browser objects are represented as opaque bigint handles.")
        lines.append(" */")
        lines.append("")
        
        if CUSTOM_TYPE_DEFINITIONS:
            lines.append("// ---------------------------------------------------------------------------")
            lines.append("// Custom type definitions")
            lines.append("// ---------------------------------------------------------------------------")
            lines.append("")
            for type_name, type_def in CUSTOM_TYPE_DEFINITIONS.items():
                lines.append(f"/** Type definition for {type_name} */")
                lines.append(f"export type {type_name} = {type_def};")
                lines.append("")
            lines.append("")
        
        all_function_names = []
        helper_function_names = set()
        for iface in domain.interfaces:
            if iface.handle_type and iface.wit_name not in GLOBAL_SINGLETONS:
                helper_name = f"lookup{iface.handle_pascal}"
                helper_function_names.add(helper_name)
            for func in iface.functions:
                all_function_names.append(func.ts_name)
                if func.is_async and not func.has_explicit_poll:
                    all_function_names.append(f"poll{func.pascal_name}")
        
        name_counts = Counter(all_function_names)
        duplicate_names = {name for name, count in name_counts.items() if count > 1}
        duplicate_names.update(helper_function_names)
        
        function_namespace = {}
        for iface in domain.interfaces:
            for func in iface.functions:
                if func.ts_name in duplicate_names:
                    exported_name = f"{iface.name}{func.pascal_name}"
                else:
                    exported_name = func.ts_name
                function_namespace[(iface.name, func.ts_name)] = exported_name
                
                if func.is_async and not func.has_explicit_poll:
                    poll_name = f"poll{func.pascal_name}"
                    if poll_name in duplicate_names:
                        exported_poll_name = f"{iface.name}Poll{func.pascal_name}"
                    else:
                        exported_poll_name = poll_name
                    function_namespace[(iface.name, poll_name)] = exported_poll_name

        has_async = any(f.is_async for iface in domain.interfaces for f in iface.functions)

        if has_async:
            lines.append("// ---------------------------------------------------------------------------")
            lines.append("// Async handle table for Promise-based operations")
            lines.append("// ---------------------------------------------------------------------------")
            lines.append("")
            lines.append("let _nextAsyncHandle = 1n;")
            lines.append("")
            lines.append("interface AsyncHandle<T> {")
            lines.append("  promise: Promise<T>;")
            lines.append("  result: { ok: true; value: T } | { ok: false; error: string } | null;")
            lines.append("}")
            lines.append("")
            lines.append("const _asyncHandles = new Map<bigint, AsyncHandle<unknown>>();")
            lines.append("")

        needed_synthetic_types = set()
        for iface in domain.interfaces:
            for func in iface.functions:
                key = (iface.wit_name, kebab_to_camel(func.wit_name))
                if key in HANDLE_RETURNING_FUNCTIONS:
                    target_type = HANDLE_RETURNING_FUNCTIONS[key]
                    if target_type in SYNTHETIC_HANDLE_TYPES:
                        needed_synthetic_types.add(target_type)
                
                if key in HANDLE_RETURNING_ARRAY_PROPERTIES:
                    target_list_type, element_type = HANDLE_RETURNING_ARRAY_PROPERTIES[key]
                    if element_type in SYNTHETIC_HANDLE_TYPES:
                        needed_synthetic_types.add(element_type)
                
                # Check for EVENT_HANDLER_PROPERTIES in getters
                if func.is_getter:
                    prop_name = func.browser_attr if func.browser_attr else kebab_to_camel(func.wit_name[4:] if func.wit_name.startswith("get-") else func.wit_name)
                    event_handler_key = (iface.wit_name, prop_name)
                    if event_handler_key in EVENT_HANDLER_PROPERTIES:
                        needed_synthetic_types.add("event-handler")
                
                for param in func.params:
                    param_key = (iface.wit_name, func.wit_name, param.wit_name)
                    if param_key in PARAMETER_BIGINT_TO_NUMBER_ALIAS:
                        conversion_type = PARAMETER_BIGINT_TO_NUMBER_ALIAS[param_key]
                        if isinstance(conversion_type, str):
                            if conversion_type.startswith("handle:"):
                                target_type = conversion_type[7:]
                                if target_type in SYNTHETIC_HANDLE_TYPES:
                                    needed_synthetic_types.add(target_type)
                            elif conversion_type.startswith("optional-handle:"):
                                target_type = conversion_type[16:]
                                if target_type in SYNTHETIC_HANDLE_TYPES:
                                    needed_synthetic_types.add(target_type)
                            elif conversion_type == "buffer-source":
                                needed_synthetic_types.add("buffer-source")
                            elif conversion_type == "event-listener":
                                needed_synthetic_types.add("event-listener")
                            elif conversion_type == "node-array":
                                needed_synthetic_types.add("node")
                    if param_key in PARAMETER_HANDLE_MAPPING:
                        target_iface, _ = PARAMETER_HANDLE_MAPPING[param_key]
                        if target_iface in SYNTHETIC_HANDLE_TYPES:
                            needed_synthetic_types.add(target_iface)

        generated_helpers = set()
        
        if needed_synthetic_types:
            lines.append("// ---------------------------------------------------------------------------")
            lines.append("// Synthetic handle tables for primitive/utility types")
            lines.append("// ---------------------------------------------------------------------------")
            lines.append("")
            for wit_type in sorted(needed_synthetic_types):
                if wit_type in SYNTHETIC_HANDLE_TYPES:
                    ts_type, handle_var, handle_pascal = SYNTHETIC_HANDLE_TYPES[wit_type]
                    lines.append(f"/** Handle table for {wit_type} values */")
                    lines.append(f"const _{handle_var} = new Map<bigint, {ts_type}>();")
                    lines.append(f"let _next{handle_pascal} = 1n;")
                    lines.append("")
            
            lines.append("// ---------------------------------------------------------------------------")
            lines.append("// Helper functions for handle lookups")
            lines.append("// ---------------------------------------------------------------------------")
            lines.append("")
            for wit_type in sorted(needed_synthetic_types):
                if wit_type in SYNTHETIC_HANDLE_TYPES:
                    ts_type, handle_var, handle_pascal = SYNTHETIC_HANDLE_TYPES[wit_type]
                    generated_helpers.add(handle_pascal)
                    lines.append(f"/** Lookup a {wit_type} value by handle. */")
                    lines.append(f"function lookup{handle_pascal}(handle: bigint): {ts_type} {{")
                    lines.append(f"  const obj = _{handle_var}.get(handle);")
                    lines.append("  if (obj === undefined) {")
                    lines.append(f"    throw new Error(`{wit_type} handle ${{handle}} not found`);")
                    lines.append("  }")
                    lines.append("  return obj!;")
                    lines.append("}")
                    lines.append("")
                    lines.append(f"/** Lookup an optional {wit_type} value by handle. */")
                    lines.append(f"function lookupOption{handle_pascal}(handle: bigint | undefined): {ts_type} | null {{")
                    lines.append("  if (handle === undefined || handle === 0n) {")
                    lines.append("    return null;")
                    lines.append("  }")
                    lines.append(f"  return _{handle_var}.get(handle) ?? null;")
                    lines.append("}")
                    lines.append("")

        for iface in domain.interfaces:
            lines.append("// ---------------------------------------------------------------------------")
            lines.append(f"// WIT interface: {iface.wit_name}")
            lines.append("// ---------------------------------------------------------------------------")
            lines.append("")

            if iface.type_aliases:
                for ta in iface.type_aliases:
                    lines.append(f"/** {ta.docs[0] if ta.docs else 'Type alias'} */")
                    lines.append(f"export type {ta.ts_name} = {ta.ts_type};")
                    lines.append("")

            if iface.handle_type:
                is_singleton = iface.wit_name in GLOBAL_SINGLETONS
                js_type = iface.js_type_for_handles or iface.browser_class
                
                if is_singleton:
                    global_ref = GLOBAL_SINGLETONS[iface.wit_name]
                    lines.append(f"/** Handle for global singleton {iface.browser_class} (fixed to 0n). */")
                    lines.append(f"const _{iface.handle_pascal}_HANDLE = 0n;")
                    lines.append("")
                    lines.append(f"/** Get the global {iface.browser_class} object. */")
                    lines.append(f"function getGlobal{iface.handle_pascal}(): {iface.browser_class} {{")
                    lines.append(f"  return {global_ref};")
                    lines.append("}")
                    lines.append("")
                else:
                    if iface.handle_pascal not in generated_helpers:
                        lines.append(f"/** Handle table for {iface.browser_class} instances */")
                        lines.append(f"const _{iface.handle_var} = new Map<bigint, {js_type}>();")
                        lines.append(f"let _next{iface.handle_pascal} = 1n;")
                        lines.append("")

                        if iface.create_function:
                            lines.append(f"/** Register a new {iface.browser_class} and return its handle. */")
                            lines.append(f"function register{iface.handle_pascal}(obj: {js_type}): bigint {{")
                            lines.append(f"  const handle = _next{iface.handle_pascal}++;")
                            lines.append(f"  _{iface.handle_var}.set(handle, obj);")
                            lines.append("  return handle;")
                            lines.append("}")

                        lines.append(f"/** Lookup a {iface.browser_class} by handle, throwing if not found. */")
                        lines.append(f"function lookup{iface.handle_pascal}(handle: bigint): {js_type} {{")
                        lines.append(f"  const obj = _{iface.handle_var}.get(handle);")
                        lines.append("  if (!obj) {")
                        lines.append(f"    throw new Error(`{iface.browser_class} handle ${{handle}} not found`);")
                        lines.append("  }")
                        lines.append("  return obj!;")
                        lines.append("}")
                        lines.append("")
                        lines.append(f"/** Lookup an optional {iface.browser_class} by handle. */")
                        lines.append(f"function lookupOption{iface.handle_pascal}(handle: bigint | undefined): {js_type} | null {{")
                        lines.append("  if (handle === undefined || handle === 0n) {")
                        lines.append("    return null;")
                        lines.append("  }")
                        lines.append(f"  return _{iface.handle_var}.get(handle) ?? null;")
                        lines.append("}")
                        generated_helpers.add(iface.handle_pascal)

            for func in iface.functions:
                self._render_function(lines, func, iface, has_async, function_namespace)

        lines.append("// ---------------------------------------------------------------------------")
        lines.append("// Exports")
        lines.append("// ---------------------------------------------------------------------------")
        lines.append("")
        lines.append("export default {")
        exports = []
        for iface in domain.interfaces:
            for func in iface.functions:
                exported_name = function_namespace.get((iface.name, func.ts_name), func.ts_name)
                exports.append(f"  {exported_name}")
                if func.is_async and not func.has_explicit_poll:
                    poll_key = (iface.name, f"poll{func.pascal_name}")
                    exported_poll_name = function_namespace.get(poll_key, f"poll{func.pascal_name}")
                    exports.append(f"  {exported_poll_name}")
        lines.append(",\n".join(exports))
        lines.append("};")
        lines.append("")

        return "\n".join(lines)

    def _render_function(
        self,
        lines: List[str],
        func: GeneratedFunction,
        iface: GeneratedInterface,
        has_async_table: bool,
        function_namespace: dict
    ) -> None:
        """Render a single function."""

        exported_name = function_namespace.get((iface.name, func.ts_name), func.ts_name)
        
        poll_name = None
        if func.is_async and not func.has_explicit_poll:
            poll_name = function_namespace.get((iface.name, f"poll{func.pascal_name}"), f"poll{func.pascal_name}")

        docs = func.docs[0] if func.docs else f"`{func.wit_name}()` operation."
        lines.append("/**")
        lines.append(f" * {docs}")
        if func.is_async and poll_name:
            lines.append(" *")
            lines.append(" * Async operation: returns request ID, poll with " +
                        f"`{poll_name}()`")
        lines.append(" */")

        params_str = ", ".join(f"{p.name}: {p.ts_type}" for p in func.params)
        lines.append(f"export function {exported_name}({params_str}): {func.ts_return} {{")

        if func.is_async:
            self._render_async_body(lines, func, iface)
        elif func.is_getter:
            self._render_getter_body(lines, func, iface)
        elif func.is_setter:
            self._render_setter_body(lines, func, iface)
        elif func.is_static:
            self._render_static_body(lines, func, iface)
        else:
            self._render_method_body(lines, func, iface)

        lines.append("}")
        lines.append("")

        if func.is_async and not func.has_explicit_poll:
            self._render_poll_function(lines, func, iface, function_namespace)
            lines.append("")

    def _render_async_body(self, lines: List[str], func: GeneratedFunction, iface: GeneratedInterface) -> None:
        """Render async function body."""
        lines.append("  const requestId = _nextAsyncHandle++;")
        
        if func.is_global_singleton:
            obj_ref = func.browser_class
            args = self._get_converted_browser_args(func, iface, skip_self=True)
            lines.append(f"  const promise = {obj_ref}.{func.browser_method}({args})")
        elif func.is_static:
            obj_ref = func.browser_class
            args = self._get_converted_browser_args(func, iface, skip_self=False)
            key = (iface.name, func.ts_name)
            if key in STATIC_METHOD_NEEDS_TYPE_ASSERTION:
                lines.append(f"  const promise = (globalThis as any).{obj_ref}.{func.browser_method}({args})")
            else:
                lines.append(f"  const promise = {obj_ref}.{func.browser_method}({args})")
        elif iface.handle_type and func.params:
            lines.append(f"  const obj = lookup{iface.handle_pascal}({func.self_param});")
            obj_ref = "obj"
            args = self._get_converted_browser_args(func, iface, skip_self=True)
            needs_type_assertion = (iface.wit_name, func.browser_method) in PROPERTIES_NEEDING_TYPE_ASSERTION
            obj_ref_with_assertion = f"({obj_ref} as any)" if needs_type_assertion else obj_ref
            lines.append(f"  const promise = {obj_ref_with_assertion}.{func.browser_method}({args})")
        else:
            lines.append("  // No handle lookup needed")
            obj_ref = "" if func.is_global_singleton else "obj"
            args = self._get_converted_browser_args(func, iface, skip_self=False)
            if obj_ref:
                lines.append(f"  const promise = {obj_ref}.{func.browser_method}({args})")
            else:
                lines.append(f"  const promise = {func.browser_class}.{func.browser_method}({args})"
                            if func.browser_class else
                            f"  // TODO: Implement async operation {func.wit_name}")

        lines.append("    .then((result: unknown) => {")
        lines.append("      const entry = _asyncHandles.get(requestId);")
        lines.append("      if (entry) {")
        if func.return_is_void:
            lines.append("        entry.result = { ok: true };")
        else:
            lines.append("        entry.result = { ok: true, value: result };")
        lines.append("      }")
        lines.append("    })")
        lines.append("    .catch((err: Error) => {")
        lines.append("      const entry = _asyncHandles.get(requestId);")
        lines.append("      if (entry) {")
        lines.append("        entry.result = { ok: false, error: err.message };")
        lines.append("      }")
        lines.append("    });")
        lines.append("")
        lines.append("  _asyncHandles.set(requestId, { promise, result: null });")
        lines.append("  return requestId;")

    def _render_getter_body(self, lines: List[str], func: GeneratedFunction, iface: GeneratedInterface) -> None:
        """Render getter function body."""
        if func.is_global_singleton:
            obj_ref = func.browser_class
        elif func.is_static:
            obj_ref = func.browser_class
        elif iface.handle_type and func.params:
            lines.append(f"  const obj = lookup{iface.handle_pascal}({func.self_param});")
            obj_ref = "obj"
        else:
            obj_ref = "obj"
        
        prop_name = func.browser_attr if func.browser_attr else kebab_to_camel(func.wit_name[4:] if func.wit_name.startswith("get-") else func.wit_name)
        needs_type_assertion = (iface.wit_name, prop_name) in PROPERTIES_NEEDING_TYPE_ASSERTION
        obj_ref_with_assertion = f"({obj_ref} as any)" if needs_type_assertion else obj_ref
        
        handle_key = (iface.wit_name, kebab_to_camel(func.wit_name))
        
        if func.is_getter_but_method:
            method_name = func.browser_method
            args = self._get_converted_browser_args(func, iface, skip_self=True)
            enum_key = (iface.wit_name, prop_name)
            if handle_key in HANDLE_RETURNING_FUNCTIONS:
                target_iface = HANDLE_RETURNING_FUNCTIONS[handle_key]
                target_var, target_pascal = self._get_handle_info(target_iface)
                lines.append(f"  const _callResult = {obj_ref_with_assertion}.{method_name}({args});")
                if func.return_is_optional:
                    lines.append(f"  if (_callResult === null) return undefined;")
                lines.append(f"  const handle = _next{target_pascal}++;")
                lines.append(f"  _{target_var}.set(handle, _callResult);")
                lines.append("  return handle;")
            elif handle_key in HANDLE_RETURNING_ARRAY_PROPERTIES:
                target_list_type, element_type = HANDLE_RETURNING_ARRAY_PROPERTIES[handle_key]
                if element_type in SYNTHETIC_HANDLE_TYPES:
                    elem_ts_type, elem_var, elem_pascal = SYNTHETIC_HANDLE_TYPES[element_type]
                    lines.append(f"  const _arr = {obj_ref_with_assertion}.{method_name}({args});")
                    lines.append(f"  const _handles: bigint[] = [];")
                    lines.append(f"  for (const item of _arr) {{")
                    lines.append(f"    const handle = _next{elem_pascal}++;")
                    lines.append(f"    _{elem_var}.set(handle, item);")
                    lines.append(f"    _handles.push(handle);")
                    lines.append(f"  }}")
                    lines.append(f"  return _handles;")
                else:
                    lines.append(f"  return [...{obj_ref_with_assertion}.{method_name}({args})];")
            elif enum_key in ENUM_PROPERTIES:
                enum_name = ENUM_PROPERTIES[enum_key]
                enum_values = ENUM_VALUE_MAPPINGS.get(enum_name, {})
                lines.append(f"  const value = {obj_ref_with_assertion}.{method_name}({args});")
                first_value = list(enum_values.keys())[0] if enum_values else ""
                switch_expr = "(value as any)" if first_value == "" else "value"
                lines.append(f"  switch ({switch_expr}) {{")
                for enum_str, enum_int in enum_values.items():
                    lines.append(f"    case '{enum_str}': return {enum_int}n;")
                lines.append(f"    default: return 0n;")
                lines.append(f"  }}")
            elif (iface.wit_name, func.wit_name) in NUMBER_TO_BIGINT_PROPERTIES:
                if func.return_is_optional:
                    lines.append(f"  const _callResult = {obj_ref_with_assertion}.{method_name}({args});")
                    lines.append(f"  return _callResult !== null ? BigInt(_callResult) : undefined;")
                else:
                    lines.append(f"  return BigInt({obj_ref_with_assertion}.{method_name}({args}));")
            elif (iface.wit_name, func.wit_name) in BOOLEAN_TO_BIGINT_PROPERTIES:
                if func.return_is_optional:
                    lines.append(f"  const _callResult = {obj_ref_with_assertion}.{method_name}({args});")
                    lines.append(f"  return _callResult != null ? (_callResult ? 1n : 0n) : undefined;")
                else:
                    lines.append(f"  return {obj_ref_with_assertion}.{method_name}({args}) ? 1n : 0n;")
            elif func.return_is_optional:
                lines.append(f"  return {obj_ref_with_assertion}.{method_name}({args}) ?? undefined;")
            elif func.return_is_void:
                lines.append(f"  {obj_ref_with_assertion}.{method_name}({args});")
            else:
                lines.append(f"  return {obj_ref_with_assertion}.{method_name}({args});")
        elif handle_key in HANDLE_RETURNING_FUNCTIONS:
            target_iface = HANDLE_RETURNING_FUNCTIONS[(iface.wit_name, kebab_to_camel(func.wit_name))]
            target_var, target_pascal = self._get_handle_info(target_iface)
            readonly_array_key = (iface.wit_name, prop_name)
            if readonly_array_key in READONLY_ARRAY_PROPERTIES:
                lines.append(f"  const _callResult = [...{obj_ref_with_assertion}.{func.browser_attr}];")
            else:
                lines.append(f"  const _callResult = {obj_ref_with_assertion}.{func.browser_attr};")
            if func.return_is_optional:
                lines.append(f"  if (_callResult === null) return undefined;")
            lines.append(f"  const handle = _next{target_pascal}++;")
            non_null_key = (iface.wit_name, func.wit_name)
            if non_null_key in GETTER_HANDLE_NON_NULL_ASSERTION:
                lines.append(f"  _{target_var}.set(handle, _callResult!);")
            else:
                lines.append(f"  _{target_var}.set(handle, _callResult);")
            lines.append("  return handle;")
        else:
            enum_key = (iface.wit_name, prop_name)
            bool_key = (iface.wit_name, func.wit_name)
            readonly_array_key = (iface.wit_name, prop_name)
            handle_array_key = handle_key
            if enum_key in ENUM_PROPERTIES:
                enum_name = ENUM_PROPERTIES[enum_key]
                enum_values = ENUM_VALUE_MAPPINGS.get(enum_name, {})
                lines.append(f"  const value = {obj_ref_with_assertion}.{func.browser_attr};")
                first_value = list(enum_values.keys())[0] if enum_values else ""
                switch_expr = "(value as any)" if first_value == "" else "value"
                lines.append(f"  switch ({switch_expr}) {{")
                for enum_str, enum_int in enum_values.items():
                    lines.append(f"    case '{enum_str}': return {enum_int}n;")
                lines.append(f"    default: return 0n;")
                lines.append(f"  }}")
            elif handle_array_key in HANDLE_RETURNING_ARRAY_PROPERTIES:
                target_list_type, element_type = HANDLE_RETURNING_ARRAY_PROPERTIES[handle_array_key]
                if element_type in SYNTHETIC_HANDLE_TYPES:
                    elem_ts_type, elem_var, elem_pascal = SYNTHETIC_HANDLE_TYPES[element_type]
                    lines.append(f"  const _arr = {obj_ref_with_assertion}.{func.browser_attr};")
                    lines.append(f"  const _handles: bigint[] = [];")
                    lines.append(f"  for (const item of _arr) {{")
                    lines.append(f"    const handle = _next{elem_pascal}++;")
                    lines.append(f"    _{elem_var}.set(handle, item);")
                    lines.append(f"    _handles.push(handle);")
                    lines.append(f"  }}")
                    lines.append(f"  return _handles;")
                else:
                    lines.append(f"  return [...{obj_ref_with_assertion}.{func.browser_attr}];")
            elif readonly_array_key in READONLY_ARRAY_PROPERTIES:
                lines.append(f"  return [...{obj_ref_with_assertion}.{func.browser_attr}];")
            elif enum_key in NUMBER_TO_BIGINT_PROPERTIES:
                if func.return_is_optional:
                    lines.append(f"  const value = {obj_ref_with_assertion}.{func.browser_attr};")
                    lines.append(f"  return value != null ? BigInt(value) : undefined;")
                else:
                    lines.append(f"  return BigInt({obj_ref_with_assertion}.{func.browser_attr});")
            elif bool_key in BOOLEAN_TO_BIGINT_PROPERTIES:
                if func.return_is_optional:
                    lines.append(f"  const value = {obj_ref_with_assertion}.{func.browser_attr};")
                    lines.append(f"  return value != null ? (value ? 1n : 0n) : undefined;")
                else:
                    lines.append(f"  return {obj_ref_with_assertion}.{func.browser_attr} ? 1n : 0n;")
            elif enum_key in EVENT_HANDLER_PROPERTIES:
                lines.append(f"  const handler = {obj_ref_with_assertion}.{func.browser_attr};")
                lines.append(f"  if (handler == null) return 0n;")
                lines.append(f"  const handle = _nextEventHandler++;")
                lines.append(f"  _eventHandlerHandles.set(handle, handler);")
                lines.append(f"  return handle;")
            elif func.return_is_optional:
                lines.append(f"  return {obj_ref_with_assertion}.{func.browser_attr} ?? undefined;")
            else:
                getter_key = (iface.wit_name, func.wit_name)
                if getter_key in GETTER_RETURN_COALESCING:
                    default_value = GETTER_RETURN_COALESCING[getter_key]
                    if isinstance(default_value, str):
                        lines.append(f"  return {obj_ref_with_assertion}.{func.browser_attr} ?? \"{default_value}\";")
                    else:
                        lines.append(f"  return {obj_ref_with_assertion}.{func.browser_attr} ?? {default_value};")
                else:
                    lines.append(f"  return {obj_ref_with_assertion}.{func.browser_attr};")

    def _render_setter_body(self, lines: List[str], func: GeneratedFunction, iface: GeneratedInterface) -> None:
        """Render setter function body."""
        if func.is_global_singleton:
            obj_ref = func.browser_class
        elif iface.handle_type and func.params:
            lines.append(f"  const obj = lookup{iface.handle_pascal}({func.self_param});")
            obj_ref = "obj"
        else:
            obj_ref = "obj"
        
        prop_name = func.browser_attr if func.browser_attr else kebab_to_camel(func.wit_name[4:] if func.wit_name.startswith("set-") else func.wit_name)
        needs_type_assertion = (iface.wit_name, prop_name) in PROPERTIES_NEEDING_TYPE_ASSERTION
        obj_ref_with_assertion = f"({obj_ref} as any)" if needs_type_assertion else obj_ref
        
        if func.is_setter_but_method:
            args = self._get_converted_browser_args(func, iface, skip_self=True)
            setter_method_key = (iface.wit_name, func.wit_name[4:] if func.wit_name.startswith("set-") else func.wit_name)
            method_name = SETTER_METHOD_NAMES.get(setter_method_key, func.browser_method)
            if func.return_is_void:
                lines.append(f"  {obj_ref_with_assertion}.{method_name}({args});")
            elif func.return_is_optional:
                _result_expr = f"{obj_ref_with_assertion}.{method_name}({args})"
                key = (iface.wit_name, kebab_to_camel(func.wit_name))
                if key in HANDLE_RETURNING_FUNCTIONS:
                    target_iface = HANDLE_RETURNING_FUNCTIONS[key]
                    target_var, target_pascal = self._get_handle_info(target_iface)
                    lines.append(f"  const _callResult = {_result_expr};")
                    lines.append(f"  if (_callResult === null) return undefined;")
                    lines.append(f"  const handle = _next{target_pascal}++;")
                    lines.append(f"  _{target_var}.set(handle, _callResult);")
                    lines.append("  return handle;")
                else:
                    lines.append(f"  return {_result_expr} ?? undefined;")
            else:
                _result_expr = f"{obj_ref_with_assertion}.{method_name}({args})"
                key = (iface.wit_name, kebab_to_camel(func.wit_name))
                if key in HANDLE_RETURNING_FUNCTIONS:
                    target_iface = HANDLE_RETURNING_FUNCTIONS[key]
                    target_var, target_pascal = self._get_handle_info(target_iface)
                    lines.append(f"  const _callResult = {_result_expr};")
                    lines.append(f"  const handle = _next{target_pascal}++;")
                    lines.append(f"  _{target_var}.set(handle, _callResult);")
                    lines.append("  return handle;")
                else:
                    lines.append(f"  return {_result_expr};")
        elif func.params:
            value_param = func.params[-1].name
            value_expr = value_param
            
            # Check config for parameter conversion
            param_key = (iface.wit_name, func.wit_name, func.params[-1].wit_name)
            if param_key in PARAMETER_BIGINT_TO_NUMBER_ALIAS:
                conversion_type = PARAMETER_BIGINT_TO_NUMBER_ALIAS[param_key]
                if isinstance(conversion_type, str) and conversion_type.startswith("optional-handle:"):
                    target_type = conversion_type[16:]
                    _, target_pascal = self._get_handle_info(target_type)
                    value_expr = f"lookupOption{target_pascal}({value_param})"
                elif isinstance(conversion_type, str) and conversion_type.startswith("optional-handle-strict:"):
                    target_type = conversion_type[23:]
                    _, target_pascal = self._get_handle_info(target_type)
                    value_expr = f"lookupOption{target_pascal}({value_param}) as any"
                elif isinstance(conversion_type, str) and conversion_type.startswith("handle:"):
                    target_type = conversion_type[7:]
                    _, target_pascal = self._get_handle_info(target_type)
                    value_expr = f"lookup{target_pascal}({value_param})"
                elif conversion_type == "string-or-null":
                    value_expr = f"{value_param} ?? null"
                elif conversion_type == "number-or-null":
                    value_expr = f"{value_param} ?? null"
                elif conversion_type == "boolean-or-false":
                    value_expr = f"{value_param} ?? false"
                elif conversion_type in ("enum-string", "event-handler"):
                    value_expr = f"{value_param} as any"
                elif isinstance(conversion_type, str) and conversion_type.startswith("string"):
                    value_expr = f"{value_param} as any"
                elif conversion_type == True:
                    value_expr = f"Number({value_param})"
                elif isinstance(conversion_type, str) and conversion_type.startswith("handle-array:"):
                    target_type = conversion_type[13:]
                    _, target_pascal = self._get_handle_info(target_type)
                    value_expr = f"Array.from({value_param}).map((h: bigint) => lookup{target_pascal}(h))"
            
            # Check if this is an event handler setter
            enum_key = (iface.wit_name, prop_name)
            if enum_key in EVENT_HANDLER_PROPERTIES:
                value_expr = f"{value_param} as any"
            # Check if this is an enum setter
            elif enum_key in ENUM_SETTER_PROPERTIES:
                enum_name = ENUM_SETTER_PROPERTIES[enum_key]
                enum_values = ENUM_VALUE_MAPPINGS.get(enum_name, {})
                # Generate reverse mapping (int -> string)
                lines.append(f"  const _enumInput = {value_param};")
                first_value = list(enum_values.keys())[0]
                # Check if first enum value is empty string - needs type assertion
                needs_type_assertion = first_value == ""
                if needs_type_assertion:
                    lines.append(f"  let enumValue: {enum_name} | '';")
                else:
                    lines.append(f"  let enumValue: {enum_name};")
                for enum_str, enum_int in enum_values.items():
                    lines.append(f"  if (_enumInput === {enum_int}n) {{ enumValue = '{enum_str}'; }}")
                lines.append(f"  else {{ enumValue = '{first_value}'; }}")
                if needs_type_assertion:
                    value_expr = "enumValue as any"
                else:
                    value_expr = "enumValue"
            elif func.params[-1].needs_handle_lookup and func.params[-1].target_handle_pascal:
                # Check if it's optional
                if func.params[-1].ts_type.endswith("| undefined"):
                    value_expr = f"lookupOption{func.params[-1].target_handle_pascal}({value_param})"
                else:
                    value_expr = f"lookup{func.params[-1].target_handle_pascal}({value_param})"
            lines.append(f"  {obj_ref_with_assertion}.{func.browser_attr} = {value_expr};")

    def _render_static_body(self, lines: List[str], func: GeneratedFunction, iface: GeneratedInterface) -> None:
        """Render static function body."""
        bool_key = (iface.wit_name, func.wit_name)
        key = (iface.name, func.ts_name)
        handle_key = (iface.wit_name, kebab_to_camel(func.wit_name))
        if func.browser_class:
            args = self._get_converted_browser_args(func, iface, skip_self=False)
            class_ref = f"(globalThis as any).{func.browser_class}" if key in STATIC_METHOD_NEEDS_TYPE_ASSERTION else func.browser_class
            if bool_key in BOOLEAN_TO_BIGINT_PROPERTIES:
                lines.append(f"  return {class_ref}.{func.browser_method}({args}) ? 1n : 0n;")
            elif func.return_is_handle and handle_key in HANDLE_RETURNING_FUNCTIONS:
                target_iface = HANDLE_RETURNING_FUNCTIONS[handle_key]
                target_var, target_pascal = self._get_handle_info(target_iface)
                lines.append(f"  const _callResult = {class_ref}.{func.browser_method}({args});")
                if func.return_is_optional:
                    lines.append(f"  if (_callResult === null) return undefined;")
                lines.append(f"  const handle = _next{target_pascal}++;")
                lines.append(f"  _{target_var}.set(handle, _callResult);")
                lines.append("  return handle;")
            else:
                lines.append(f"  return {class_ref}.{func.browser_method}({args});")
        else:
            lines.append(f"  // Static operation: {func.wit_name}")
            lines.append(f"  throw new Error('Static operation not implemented: {func.wit_name}');")

    def _render_method_body(self, lines: List[str], func: GeneratedFunction, iface: GeneratedInterface) -> None:
        """Render instance method body."""
        if func.is_global_singleton:
            obj_ref = func.browser_class
        elif iface.handle_type and func.params:
            lines.append(f"  const obj = lookup{iface.handle_pascal}({func.self_param});")
            obj_ref = "obj"
        else:
            obj_ref = "" if func.is_global_singleton else "obj"
        
        args = self._get_converted_browser_args(func, iface, skip_self=True)
        
        method_name = func.browser_method if func.browser_method else kebab_to_camel(func.wit_name)
        needs_type_assertion = (iface.wit_name, method_name) in PROPERTIES_NEEDING_TYPE_ASSERTION
        obj_ref_with_assertion = f"({obj_ref} as any)" if needs_type_assertion and obj_ref else obj_ref

        enum_key = (iface.wit_name, method_name)
        if func.return_is_void:
            lines.append(f"  {obj_ref_with_assertion}.{func.browser_method}({args});")
        elif enum_key in ENUM_PROPERTIES:
            enum_name = ENUM_PROPERTIES[enum_key]
            enum_values = ENUM_VALUE_MAPPINGS.get(enum_name, {})
            lines.append(f"  const value = {obj_ref_with_assertion}.{func.browser_method}({args});")
            first_value = list(enum_values.keys())[0] if enum_values else ""
            switch_expr = "(value as any)" if first_value == "" else "value"
            lines.append(f"  switch ({switch_expr}) {{")
            for enum_str, enum_int in enum_values.items():
                lines.append(f"    case '{enum_str}': return {enum_int}n;")
            lines.append(f"    default: return 0n;")
            lines.append(f"  }}")
        elif (iface.wit_name, func.wit_name) in BOOLEAN_TO_BIGINT_PROPERTIES:
            lines.append(f"  return {obj_ref_with_assertion}.{func.browser_method}({args}) ? 1n : 0n;")
        elif (iface.wit_name, func.wit_name) in NUMBER_TO_BIGINT_PROPERTIES:
            lines.append(f"  return BigInt({obj_ref_with_assertion}.{func.browser_method}({args}));")
        elif func.return_is_handle:
            key = (iface.wit_name, kebab_to_camel(func.wit_name))
            if key in HANDLE_RETURNING_ARRAY_PROPERTIES:
                target_list_type, element_type = HANDLE_RETURNING_ARRAY_PROPERTIES[key]
                if element_type in SYNTHETIC_HANDLE_TYPES:
                    elem_ts_type, elem_var, elem_pascal = SYNTHETIC_HANDLE_TYPES[element_type]
                    lines.append(f"  const _arr = {obj_ref_with_assertion}.{func.browser_method}({args});")
                    lines.append(f"  const _handles: bigint[] = [];")
                    lines.append(f"  for (const item of _arr) {{")
                    lines.append(f"    const handle = _next{elem_pascal}++;")
                    lines.append(f"    _{elem_var}.set(handle, item);")
                    lines.append(f"    _handles.push(handle);")
                    lines.append(f"  }}")
                    lines.append(f"  return _handles;")
                else:
                    lines.append(f"  return [...{obj_ref_with_assertion}.{func.browser_method}({args})];")
            elif key in HANDLE_RETURNING_FUNCTIONS:
                target_iface = HANDLE_RETURNING_FUNCTIONS[key]
                target_var, target_pascal = self._get_handle_info(target_iface)
                lines.append(f"  const _callResult = {obj_ref_with_assertion}.{func.browser_method}({args});")
                if func.return_is_optional:
                    lines.append(f"  if (_callResult === null) return undefined;")
                lines.append(f"  const handle = _next{target_pascal}++;")
                lines.append(f"  _{target_var}.set(handle, _callResult);")
                lines.append("  return handle;")
            else:
                lines.append(f"  return {obj_ref_with_assertion}.{func.browser_method}({args});")
        elif func.return_is_optional:
            lines.append(f"  return {obj_ref_with_assertion}.{func.browser_method}({args}) ?? undefined;")
        else:
            lines.append(f"  return {obj_ref_with_assertion}.{func.browser_method}({args});")

    def _render_poll_function(
        self,
        lines: List[str],
        func: GeneratedFunction,
        iface: GeneratedInterface,
        function_namespace: dict
    ) -> None:
        """Render poll function for async operations."""
        poll_ts_name = f"poll{func.pascal_name}"
        exported_poll_name = function_namespace.get((iface.name, poll_ts_name), poll_ts_name)

        lines.append("/**")
        lines.append(f" * Poll an async `{func.ts_name}()` operation.")
        lines.append(" * Returns undefined if still pending, or the result if complete.")
        lines.append(" */")

        if func.return_is_void or not func.ts_return_inner:
            result_type = "{ ok: true } | { ok: false; error: string } | undefined"
        else:
            result_type = f"{{ ok: true; value: {func.ts_return_inner} }} | {{ ok: false; error: string }} | undefined"

        lines.append(f"export function {exported_poll_name}(requestId: bigint): {result_type} {{")
        lines.append("  const entry = _asyncHandles.get(requestId);")
        lines.append("  if (!entry) {")
        lines.append("    return { ok: false, error: `Unknown request ID ${requestId}` };")
        lines.append("  }")
        if func.return_is_void or not func.ts_return_inner:
            lines.append("  return entry.result ?? undefined;")
        else:
            lines.append(f"  return entry.result as {{ ok: true; value: {func.ts_return_inner} }} | {{ ok: false; error: string }} | null ?? undefined;")
        lines.append("}")

    def render_index(self, domains: List[GeneratedDomain]) -> str:
        """Render the main index.ts file."""

        total_interfaces = sum(d.interface_count for d in domains)
        total_functions = sum(
            len(f) for d in domains
            for iface in d.interfaces
            for f in [iface.functions]
        )

        lines: List[str] = []

        lines.append("/**")
        lines.append(" * @tairitsu/browser-glue/generated")
        lines.append(" *")
        lines.append(" * Auto-generated TypeScript glue code for browser WIT interfaces.")
        lines.append(" *")
        lines.append(" * Generated by: scripts/generate_browser_glue.py")
        lines.append(" * Source: packages/browser-worlds/wit/generated/*.wit")
        lines.append(" *")
        lines.append(" * DO NOT EDIT MANUALLY - regenerate with: just glue-gen")
        lines.append(" *")
        lines.append(" * ## Package layout")
        lines.append(" *")

        for domain in domains:
            lines.append(f" * - `./{domain.name}` — `tairitsu-browser:{domain.name}` " +
                        f"({domain.interface_count} interfaces)")

        lines.append(" */")
        lines.append("")
        lines.append("// Re-export all generated modules")

        for domain in domains:
            lines.append(f"export * as {domain.export_name} from \"./{domain.export_name}\";")

        # Add manual modules (interfaces defined in browser-full.wit but not in generated/*.wit)
        lines.append("")
        lines.append("// Manual modules (interfaces defined in browser-full.wit but not in generated/*.wit)")
        lines.append("export * as console from \"./console\";")
        lines.append("export * as style from \"./style\";")
        lines.append("export { addEventListener, removeEventListener, preventDefault, stopPropagation } from \"./event-target\";")

        lines.append("")
        lines.append("// Statistics")
        lines.append(f"export const GLUE_STATS = {{")
        lines.append(f"  totalDomains: {len(domains)},")
        lines.append(f"  totalInterfaces: {total_interfaces},")
        lines.append(f"  totalFunctions: {total_functions},")
        lines.append(f"  generatedAt: \"{datetime.now().isoformat()}\",")
        lines.append("};")
        lines.append("")

        return "\n".join(lines)

    def _get_converted_browser_args(
        self,
        func: GeneratedFunction,
        iface: GeneratedInterface,
        skip_self: bool = True
    ) -> str:
        """Generate converted browser arguments with handle lookups and type conversions."""
        converted_args = []
        for param in func.params:
            if skip_self and param.name == func.self_param:
                continue
            if func.skip_first_param and param.name == func.params[0].name:
                continue
            
            skip_key = (iface.wit_name, func.wit_name, param.wit_name)
            if skip_key in PARAMS_TO_SKIP:
                continue
            
            key = (iface.wit_name, func.wit_name, param.wit_name)
            if key in PARAMETER_BIGINT_TO_NUMBER_ALIAS:
                conversion_type = PARAMETER_BIGINT_TO_NUMBER_ALIAS[key]
                if conversion_type == "handle" or (isinstance(conversion_type, str) and conversion_type.startswith("handle:")):
                    if isinstance(conversion_type, str) and conversion_type.startswith("handle:"):
                        target_wit_type = conversion_type[7:]
                    else:
                        target_wit_type = param.wit_type_str.replace("-handle", "").replace("-list", "")
                    _, target_pascal = self._get_handle_info(target_wit_type)
                    arg_expr = f"BigInt({param.name})" if param.ts_type == "number" else param.name
                    converted_args.append(f"lookup{target_pascal}({arg_expr})")
                elif conversion_type == "optional-handle" or (isinstance(conversion_type, str) and conversion_type.startswith("optional-handle:")):
                    if isinstance(conversion_type, str) and conversion_type.startswith("optional-handle:"):
                        target_wit_type = conversion_type[16:]
                    else:
                        target_wit_type = param.wit_type_str.replace("-handle", "").replace("-list", "")
                    _, target_pascal = self._get_handle_info(target_wit_type)
                    arg_expr = f"BigInt({param.name})" if param.ts_type == "number" else param.name
                    converted_args.append(f"lookupOption{target_pascal}({arg_expr})")
                elif isinstance(conversion_type, str) and conversion_type.startswith("optional-handle-strict:"):
                    target_wit_type = conversion_type[23:]
                    _, target_pascal = self._get_handle_info(target_wit_type)
                    arg_expr = f"BigInt({param.name})" if param.ts_type == "number" else param.name
                    converted_args.append(f"lookupOption{target_pascal}({arg_expr}) as any")
                elif conversion_type == "array":
                    converted_args.append(f"Array.from({param.name}).map(Number)")
                elif conversion_type == "boolean":
                    converted_args.append(f"Boolean({param.name})")
                elif isinstance(conversion_type, str) and conversion_type.startswith("dictionary:"):
                    converted_args.append(f"{param.name} as any")
                elif conversion_type == "string-from-array":
                    converted_args.append(f"String.fromCodePoint(...Array.from({param.name}).map(Number))")
                elif conversion_type == "buffer-source":
                    converted_args.append(f"lookupBufferSource({param.name})")
                elif conversion_type == "event-listener":
                    converted_args.append(f"{param.name} !== undefined ? lookupEventListener({param.name}) : null")
                elif isinstance(conversion_type, str) and conversion_type.startswith("enum:"):
                    converted_args.append(f"{param.name} as any")
                elif isinstance(conversion_type, str) and conversion_type.startswith("string"):
                    converted_args.append(f"{param.name} as any")
                elif conversion_type == "nullable-string":
                    converted_args.append(f"{param.name} ?? null")
                elif conversion_type == "node-array":
                    converted_args.append(f"Array.from({param.name}).map((h: bigint) => lookupNode(h))")
                elif conversion_type == "nullable-buffer-source":
                    converted_args.append(f"{param.name} ?? null")
                elif conversion_type == "buffer-source-or-string":
                    converted_args.append(f"{param.name}")
                elif conversion_type == "event-listener-options":
                    converted_args.append(f"{param.name} as any")
                elif conversion_type == "any":
                    converted_args.append(f"{param.name} as any")
                elif conversion_type == "boolean-or-undefined":
                    converted_args.append(f"{param.name} !== undefined ? Boolean({param.name}) : undefined")
                elif conversion_type == "boolean-or-false":
                    converted_args.append(f"Boolean({param.name} ?? false)")
                elif isinstance(conversion_type, str) and conversion_type.startswith("handle-array:"):
                    target_type = conversion_type[13:]
                    _, target_pascal = self._get_handle_info(target_type)
                    converted_args.append(f"Array.from({param.name}).map((h: bigint) => lookup{target_pascal}(h))")
                elif isinstance(conversion_type, str) and conversion_type.startswith("spread-handle-array:"):
                    target_type = conversion_type[20:]
                    _, target_pascal = self._get_handle_info(target_type)
                    converted_args.append(f"...Array.from({param.name}).map((h: bigint) => lookup{target_pascal}(h))")
                elif isinstance(conversion_type, str) and conversion_type.startswith("handle:event-target-list"):
                    converted_args.append(f"Array.from({param.name}).map((h: bigint) => lookupEventTarget(h))")
                elif isinstance(conversion_type, str) and conversion_type.startswith("handle:request-or-string"):
                    converted_args.append(f"lookupRequestOrString({param.name})")
                elif isinstance(conversion_type, str) and conversion_type.startswith("handle:blob-parts"):
                    converted_args.append(f"Array.from({param.name}).map((h: bigint) => lookupBlob(h))")
                elif isinstance(conversion_type, str) and conversion_type.startswith("handle:blob-or-media-source"):
                    converted_args.append(f"lookupBlobOrMediaSource({param.name})")
                elif isinstance(conversion_type, str) and conversion_type.startswith("handle:canvas-image-source"):
                    converted_args.append(f"lookupCanvasImageSource({param.name})")
                elif isinstance(conversion_type, str) and conversion_type.startswith("handle:clipboard-items"):
                    converted_args.append(f"Array.from({param.name}).map((h: bigint) => lookupClipboardItem(h))")
                elif isinstance(conversion_type, str) and conversion_type.startswith("string-or-url"):
                    converted_args.append(f"{param.name} as any")
